    // Матчинг по границам слова, чтобы «МАКСИМА» не совпадала с «максимального»,
    // «Мотив» — с «мотивированное» и т.п. (предкомпилировано один раз при старте, не на
    // каждый вызов — иначе на 112 тыс. документов пересборка Regex из строки многократно
    // перевешивала бы саму работу по извлечению данных).
    private static readonly (Regex Rx, string Key)[] Matchers =
        [.. All.Select(k => (new Regex($@"\b{Regex.Escape(k)}\b",
            RegexOptions.IgnoreCase | RegexOptions.Compiled), k))];

    // Отдельный набор — ключи Canonical пересекаются с All, но не идентичны им (часть операторов
    // в All не имеет канонической формы). Раньше Canonicalize собирал новый Regex из строки на
    // КАЖДОЙ из ~45 записей при КАЖДОМ вызове (а вызывается он для каждого найденного оператора
    // на 112 тыс. документов) — самый горячий путь в OperatorExtractor.
    private static readonly (Regex Rx, string Value)[] CanonicalMatchers =
        [.. Canonical.Select(kvp => (new Regex($@"\b{Regex.Escape(kvp.Key)}\b",
            RegexOptions.IgnoreCase | RegexOptions.Compiled), kvp.Value))];

    public static bool IsKnown(string candidate) =>
        Matchers.Any(m => m.Rx.IsMatch(candidate));

    public static string? FindInText(string text)
    {
        foreach (var (rx, key) in Matchers)
            if (rx.IsMatch(text))
                return Canonical.TryGetValue(key, out var name) ? name : key;
        return null;
    }

    public static string? Canonicalize(string raw)
    {
        foreach (var (rx, value) in CanonicalMatchers)
            if (rx.IsMatch(raw))
                return value;
        return null;
    }
