        // «базовой станции PZ 0160"» — латинские буквы + пробел + цифры (10-КЦ-01, Карелия)
        (@"[Бб]азов\w*\s+станци\w*\s+([A-Z]{1,4}\s+\d{3,5})\b(?!\s*(?:км|м\b|МГц|Вт))", 1),
        // «беспроводного сегмента ЕМТС № 28» — номер сегмента ЕМТС (78-01-05, СПб)
        (@"сегмента\s+(ЕМТС\s*№\s*\d{1,4})\b", 1),
