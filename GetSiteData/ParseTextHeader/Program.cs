    // ── Entry point ────────────────────────────────────────────────────

    public static async Task Main()
    {
        LoadConfiguration();

        Log.Phase("Старт парсера документов");
        Log.Info($"Входная директория : {InputBasePath}");
        Log.Info($"Выходная (успешные): {OutputJsonPath}");
        Log.Info($"Выходная (ошибки)  : {OutputErrorsPath}");
        Log.Info($"Параллельных потоков: {MaxParallelism}");
        Log.Info($"Нормализация Dadata : {(UseDadata ? "ВКЛЮЧЕНА" : "ОТКЛЮЧЕНА (отладочный режим)")}");

        Directory.CreateDirectory(OutputJsonPath);
        Directory.CreateDirectory(OutputErrorsPath);

        // Загружаем логи всех существующих подкаталогов заранее
        LoadAllProcessedLogs();

        // Единый формат конвейера: берём только файлы с именем «Номер заключения
        // и дата» (Common.DocumentName) — служебные и посторонние не трогаем.
        var allFiles = Directory
            .EnumerateFiles(InputBasePath, "*.txt", SearchOption.AllDirectories)
            .Where(DocumentName.IsValidFileName)
            .Select(f => new FileInfo(f))
            .OrderByDescending(f => f.FullName)
            .ToList();

        Log.Info($"Найдено файлов    : {allFiles.Count}");
        Log.Info($"Уже обработано    : {_processedByDir.Values.Sum(s => s.Count)}");

        var toProcess = allFiles.Where(f => !IsAlreadyProcessed(f)).ToList();
        Log.Info($"К обработке       : {toProcess.Count}");

        if (toProcess.Count == 0) { Log.Info("Новых файлов нет."); return; }

        long success = 0, warnings = 0, errors = 0, skipped = 0;
        long total = 0;

        await Parallel.ForEachAsync(toProcess,
            new ParallelOptions { MaxDegreeOfParallelism = MaxParallelism },
            async (fileInfo, ct) =>
            {
                try
                {
                    var result = await ProcessFileAsync(fileInfo);
                    switch (result)
                    {
                        case ProcessingResult.Success: Interlocked.Increment(ref success); break;
                        case ProcessingResult.SuccessWithErrors: Interlocked.Increment(ref warnings); break;
                        case ProcessingResult.Skipped: Interlocked.Increment(ref skipped); break;
                        case ProcessingResult.Error: Interlocked.Increment(ref errors); break;
                    }
                }
                catch (Exception ex)
                {
                    Interlocked.Increment(ref errors);
                    Log.Error($"{fileInfo.FullName}: {ex.Message}");
                }

                var n = Interlocked.Increment(ref total);
                if (n % 10 == 0)
                    Log.Info($"  Обработано: {n}/{toProcess.Count}");
            });

        // Финальный сброс — досохраняем последний неполный батч (< FlushEveryNFiles файлов).
        FlushDirtyLogs();

        // Формат итогового блока сохраняем стабильным: по этим строкам сравниваются
        // результаты прогонов между версиями парсера.
        Log.Phase("Готово:");
        Log.Info($"  Успешно (все поля)   : {success}");
        Log.Info($"  Предупреждения        : {warnings}");
        Log.Info($"  Ошибок чтения        : {errors}");
        Log.Info($"  Пропущено            : {skipped}");
    }

    // ── Configuration ──────────────────────────────────────────────────

    private static void LoadConfiguration()
    {
        // Единый appsettings.json на весь конвейер — читаем СВОЮ секцию «ParseTextHeader».
        var config = new ConfigurationBuilder()
            .SetBasePath(AppContext.BaseDirectory)
            .AddJsonFile("appsettings.json", optional: true, reloadOnChange: false)
            .AddUserSecrets(typeof(Program).Assembly, optional: true)
            .AddEnvironmentVariables()
            .Build()
            .GetSection("ParseTextHeader");

        InputBasePath = config["InputBasePath"] ?? InputBasePath;
        OutputJsonPath = config["OutputJsonPath"] ?? OutputJsonPath;
        OutputErrorsPath = config["OutputErrorsPath"] ?? OutputErrorsPath;

        if (int.TryParse(config["Processing:MaxParallelism"], out var mp) && mp > 0)
            MaxParallelism = mp;
        if (int.TryParse(config["Processing:LinesToRead"], out var lr) && lr > 0)
            LinesToRead = lr;

        UseDadata = bool.TryParse(config["Dadata:Enabled"], out var en) && en;
        if (UseDadata)
        {
            var token = config["Dadata:Token"];
            var secret = config["Dadata:Secret"];
            if (string.IsNullOrWhiteSpace(token) || string.IsNullOrWhiteSpace(secret))
            {
                Log.Warn("Dadata включена, но Token/Secret не заданы — нормализация отключена.");
                UseDadata = false;
            }
            else
            {
                _dadataApi = new CleanClientAsync(token, secret);
            }
        }
    }

    // ── Single file ────────────────────────────────────────────────────

    // Регулярная опечатка «стация»/«стации» вместо «станция»/«станции» — встречается массово в
    // одной серии документов (31-БО-16, Белгородская область). Ограничиваем контекстом «базов...»,
    // чтобы не задеть настоящее слово «стация» (биотоп/место обитания) в других текстах.
    [GeneratedRegex(@"(?<=[Бб]азов\w{0,3}\s)стаци(?=[ийя]\b)")]
    private static partial Regex StaciaTypoRx();

    private static string NormalizeKnownTypos(string text) =>
        StaciaTypoRx().Replace(text, "станци");

    // Первая строка документа — числовой порядковый номер записи.
    [GeneratedRegex(@"^(\d+)\b")]
    private static partial Regex IndexLineRx();

    // «NN.XX.NN.NNN.Т.NNNNNN.NN.NN от DD.MM.YYYY» — номер заключения и дата;
    // XX — 2-4 символа (буквы, цифры, русские буквы: РЦ, БЦ, НС, 01, 99 и т.д.)
    [GeneratedRegex(@"\d{2}\.[А-ЯЁA-Zа-яёa-z0-9]{2,4}\.\d{2}\.\d{3}\.[ТTТт]\.\d{6}\.\d{2}\.\d{2}\s+от\s+\d{2}\.\d{2}\.\d{4}", RegexOptions.IgnoreCase)]
    private static partial Regex DocNumberAndDateRx();

    private static async Task<ProcessingResult> ProcessFileAsync(FileInfo fileInfo)
    {
        var relativePath = GetRelativePath(fileInfo.FullName);
        var subDir = GetSubDir(fileInfo);   // относительный путь подкаталога

        if (IsAlreadyProcessed(fileInfo))
            return ProcessingResult.AlreadyProcessed;

        List<string> firstLines;
        string fullText;
        try
        {
            // Один проход чтения вместо двух (ReadLines + ReadAllText открывали файл дважды —
            // на 100k+ файлах лишние открытия/сканирования антивирусом заметно накапливались).
            fullText = await File.ReadAllTextAsync(fileInfo.FullName);
            fullText = NormalizeKnownTypos(fullText);
            firstLines = fullText.Split('\n').Take(LinesToRead).Select(l => l.TrimEnd('\r')).ToList();
        }
        catch (Exception ex)
        {
            Log.Error($"Чтение {relativePath}: {ex.Message}");
            return ProcessingResult.Error;
        }

        if (firstLines.Count == 0)
        {
            Log.Skip($"Пустой файл: {relativePath}");
            return ProcessingResult.Skipped;
        }

        // Собственной проверки «документ ли это БС» здесь НЕТ намеренно: отбор
        // выполняет предыдущий этап конвейера (ML-классификатор MLTextToData),
        // и всё, что пришло на вход, разбирается безусловно. Прежняя проверка
        // по ключевым словам дублировала классификатор и ошибочно отбрасывала
        // реальные документы с редкими формулировками («Проект модернизации
        // БС № 76-00109…», «условия размещения РЭС ПАО "МТС" БС № BTS-…»).
        var document = await ParseDocumentAsync(firstLines, fullText, fileInfo.Name, relativePath);
        var hasAll = document.HasAllRequiredFields();

        var jsonRelPath = Path.ChangeExtension(relativePath, ".json");
        var targetPath = hasAll
            ? Path.Combine(OutputJsonPath, jsonRelPath)
            : Path.Combine(OutputErrorsPath, jsonRelPath);

        EnsureDir(targetPath);

        WriteJson(targetPath, document);

        MarkProcessed(subDir, fileInfo.Name);

        if (hasAll)
            Log.Ok(relativePath);
        else
            Log.Warn($"{relativePath} -> нет: {string.Join(", ", document.GetMissingFields())}");

        return hasAll ? ProcessingResult.Success : ProcessingResult.SuccessWithErrors;
    }

    // ── Parser ─────────────────────────────────────────────────────────

    private static async Task<BaseStationDocument> ParseDocumentAsync(
        List<string> lines, string fullText, string fileName, string relativePath)
    {
        var doc = new BaseStationDocument
        {
            SourceFileName = fileName,
            RelativePath = relativePath,
            ProcessingDate = DateTime.Now,
            RawFirstLines = lines
        };

        var headerText = string.Join(" ", lines);

        // Index (первая строка — цифровой номер записи)
        var idxMatch = IndexLineRx().Match(lines[0]);
        if (idxMatch.Success) doc.Index = idxMatch.Groups[1].Value;

        // Номер заключения и дата
        // Формат: NN.XX.NN.NNN.Т.NNNNNN.NN.NN от DD.MM.YYYY
        // где XX — 2-4 символа (буквы, цифры, русские буквы: РЦ, БЦ, НС, 01, 99 и т.д.)
        var docNumMatch = DocNumberAndDateRx().Match(headerText);
        if (docNumMatch.Success)
            doc.DocumentNumberAndDate = docNumMatch.Value;

        // Номер базовой станции
        doc.BaseStationNumber = ExtractBaseStationNumber(fullText);

        // Адрес
        var rawAddress = AddressParser.Extract(fullText, lines);

        // Оператор (до Dadata, чтобы не зависеть от неё)
        doc.Operator = OperatorExtractor.Extract(fullText);

        // Координаты из текста
        doc.Coordinates = CoordinateParser.Extract(fullText);

        // Нормализация адреса через Dadata (только если UseDadata = true)
        if (!string.IsNullOrWhiteSpace(rawAddress))
        {
            if (UseDadata && _dadataApi != null)
            {
                try
                {
                    var cleaned = await _dadataApi.Clean<Address>(rawAddress);
                    doc.BaseStationAddress = string.IsNullOrWhiteSpace(cleaned?.result)
                        ? AddressParser.Clean(rawAddress)
                        : cleaned.result;

                    // Координаты из Dadata — только если не нашли в тексте
                    if (string.IsNullOrWhiteSpace(doc.Coordinates)
                        && !string.IsNullOrWhiteSpace(cleaned?.geo_lat)
                        && !string.IsNullOrWhiteSpace(cleaned?.geo_lon))
                    {
                        doc.Coordinates = $"{cleaned.geo_lat}, {cleaned.geo_lon}";
                    }
                }
                catch (Exception ex)
                {
                    Log.Warn($"Dadata, {relativePath}: {ex.Message}");
                    doc.BaseStationAddress = AddressParser.Clean(rawAddress);
                }
            }
            else
            {
                // Режим отладки: адрес без нормализации
                doc.BaseStationAddress = AddressParser.Clean(rawAddress);
            }
        }

        return doc;
    }

    // ── Base station number ─────────────────────────────────────────────

    private static string? ExtractBaseStationNumber(string text)
    {
        // 1. Сначала ищем в «Проектной документации» — там номер принадлежит основному оператору
        //    и не содержит номеров сторонних БС (как в Приложении)
        var projStart = text.IndexOf("Проектная документация", StringComparison.OrdinalIgnoreCase);
        if (projStart >= 0)
        {
            var projEnd = text.IndexOf("СООТВЕТСТВУЕТ", projStart, StringComparison.OrdinalIgnoreCase);
            var projSlice = projEnd > projStart
                ? text[projStart..projEnd]
                : text[projStart..Math.Min(projStart + 1500, text.Length)];
            var result = TryExtractBsNumber(projSlice);
            if (result != null) return result;
        }

        // 2. Начало раздела «Приложение» — первые ~600 символов, до упоминания сторонних БС.
        // Регистрозависимо — см. комментарий в AddressParser.Extract про случайные строчные
        // упоминания «(приложение)» внутри текста, из-за которых срез расширялся до «Фирма-разработчик».
        var appendixStart = text.IndexOf("Приложение", StringComparison.Ordinal);
        if (appendixStart >= 0)
        {
            var shortSlice = text[appendixStart..Math.Min(appendixStart + 600, text.Length)];
            var result = TryExtractBsNumber(shortSlice);
            if (result != null) return result;

            // 2б. Полный поиск в Приложении как фоллбэк
            result = TryExtractBsNumber(text[appendixStart..]);
            if (result != null) return result;
        }

        // 3. Фоллбэк — весь текст
        return TryExtractBsNumber(text);
    }

    // Паттерны в порядке специфичности (источник; компилируются один раз ниже).
    // group=1 означает «взять Groups[1]», group=0 — «взять всё совпадение».
    private static readonly (string Pattern, int Group)[] BsNumberPatternSources =
    [
        // ── Форматы с явными префиксами BSR/BTS/BSR ──────────────────────────
        // «BSR_51_00299_LN»
        (@"\b(BSR_[A-Z0-9_]+)\b", 1),
        // «BTS-86-00895GUL9L18» — с дефисами
        (@"\b(BTS-[A-Z0-9\-]+)\b", 1),
        // «BTS_43_01058_DL18» — с подчёркиваниями; [А-ЯЁ] — кириллические омографы в суффиксах
        // («BTS_43_743_GDUL9L18МL21L26FL26Т» — М и Т кириллические, 43-ОЦ-02)
        (@"\b(BTS_[A-ZА-ЯЁ0-9_]+)\b", 1),
        // «BTS 24-01449UL18NI18L26» / «BTS 64-129GUL18» — пробел между BTS и номером (24-ЗЦ-08, 64-01-02)
        (@"\bBTS\s+(\d{2,}-\d{3,}[A-ZА-ЯЁa-zа-яё0-9]*)\b", 1),
        // «BTS- 59-00844UL18L21» — дефис и пробел после BTS (59-55-20, 2025)
        (@"\bBTS-?\s+(\d{2,3}-\d{3,}[A-Za-zА-ЯЁ0-9]*)\b", 1),
        // «BTS 6407_DON_AMV_RNK» — цифровой код + токены через подчёркивание (80-01-05, ДНР, К-ТЕЛЕКОМ)
        (@"\bBTS\s+(\d{3,}(?:_[A-ZА-ЯЁ]{2,6}){1,4})\b", 1),
        // «BTS3911Е» / «BTS3911E 2100» — модель оборудования как номер (87-01-03)
        (@"\b(BTS\d{3,}[A-ZА-ЯЁa-zа-яё]*)\b", 1),

        // ── «(БС) №» — в скобках ─────────────────────────────────────────────
        // «(БС) № 2004 "Тв-Гусева"» — числовой номер + имя в кавычках (год-фильтр неприменим)
        (@"\([БB][CСс]\)\s*№\s*(\d{3,5})\s+""[A-ZА-ЯЁa-zа-яё]", 1),
        // «БС № 2004 "имя"» / «№ 2004 "имя"» — без скобок, но с именем
        (@"[БB][CСс]\s*№\s*(\d{3,5})\s+""[A-ZА-ЯЁa-zа-яё]", 1),
        (@"(?<![A-ZА-ЯЁ0-9\-])№\s*(\d{3,5})\s+""[A-ZА-ЯЁa-zа-яё]", 1),
        // «(БС) № LE000685» — буквенно-цифровой
        (@"\([БB][CСс]\)\s*№\s*([A-ZА-ЯЁ0-9][A-ZА-ЯЁ0-9\-_/]+)", 1),

        // «базовая станция БС № 2 сети» / «БС №2 сети» — малый номер с явным контекстом
        // Добавляем ДО фильтра ^\d{1,2}$, чтобы однозначные и двузначные номера не отсеялись
        (@"[БB][CСс]\s*№\s*(\d{1,5})\s+(?:сети|стандарт)", 1),
        (@"[Бб]азов(?:ой|ая)\s+станци(?:я|и)[^№\n]{0,30}№\s*(\d{1,5})\s+(?:сети|стандарт)", 1),
        (@"[БB][CСс]\s*№?\s*""([A-ZА-ЯЁ0-9][A-ZА-ЯЁ0-9\-_/]+)""", 1),
        // «БС №61414» / «БС № LE000973» — буквенно-цифровой номер
        (@"[БB][CСс]\s*№\s*([A-ZА-ЯЁ0-9][A-ZА-ЯЁ0-9\-_/]+)", 1),
        // «БС-55239» — формат БС-ЦИФРЫ без №
        (@"[БB][CСс]-(\d{4,})\b", 1),
        // «БС - 56251» — формат с пробелами вокруг дефиса (73-ФУ-04)
        (@"[БB][CСс]\s+-\s*(\d{4,})\b", 1),
        // «БС-73-717GUL9L21» — БС-ЦИФРЫ-ЦИФРЫ+буквы
        (@"[БB][CСс]-(\d{2,3}-\d{3,}[A-ZА-ЯЁa-zа-яё0-9]*)\b", 1),
        // «БС 16_093» / «БС 16_01964» — подчёркивание вместо дефиса
        (@"[БB][CСс]\s+(\d{2,}_\d{2,}[A-ZА-ЯЁa-zа-яё0-9_]*)\b", 1),
        // «БС № 70.195» / «БС № 22.4437» / «БС № 70.85682» / «№ 42.1690_» — номер с точкой (2–5 цифр)
        // Разрешаем _ после числа (42.1690_"Кмр. Паркинг") — отбрасываем его через TrimEnd
        (@"[БB][CСс]\s*№\s*(\d{2,3}\.\d{2,5})[_]?\b", 1),
        // «БС 02-03024 L18» / «БС 23-04823 NiL18L21L26L26T» — пробел + длинный суффикс (4+ цифр)
        (@"[БB][CСс]\s+(\d{2,}-\d{4,}(?:\s+[A-Z][A-Za-z0-9]{1,20}|[A-ZА-ЯЁa-zа-яё0-9]*))\b", 1),
        // «БС 02-046 GUL9L18» — 3 цифры + пробел + буквенный суффикс (02-БЦ-01)
        (@"[БB][CСс]\s+(\d{2,3}-\d{3}\s+[A-Z][A-Za-z0-9]+)\b", 1),
        // «БС 12-96» / «БС 09-91» — короткий с дефисом (12-РЦ-01, 09-01-07)
        (@"[БB][CСс]\s+(\d{2,3}-\d{2,3})\b", 1),
        // «БС 142 км» / «БС 121 км» — нумерация по километражу ВСМ (78-ДЦ-06)
        (@"[БB][CСс]\s+(\d{2,4}\s*км)\b", 1),
        // «Базовая станция №6 (133 км)» — номер с километражем в скобках, ЯКОРЬ на «Базовая
        // станция» обязателен: без него более общий «№\s*(\d{5,})» ниже по списку раньше находил
        // «Заявка № 164262 от 22.11.2024» из раздела заявителя вместо настоящего номера станции
        // (14-01-01, Якутия).
        (@"[Бб]азовая\s+станция\s+№\s*(\d{1,2}\s*\(\d{1,4}\s*км\))", 1),
        // «БС №196» / «БС № 747» / «БС № 867» — 3-4 цифры рядом с БС/№
        (@"[БB][CСс]\s*№\s*(\d{3,4})\b", 1),
        // «БС 45610» — чисто цифровой 5+
        (@"[БB][CСс]\s+(\d{5,})\b", 1),
        // «BTS22-00256GUL18NI18L26» — BTS слитно с цифрами (без пробела/дефиса перед регионом) (22-01-03, 2024)
        (@"\bBTS(\d{2}-\d{4,}[A-ZА-ЯЁa-zа-яё0-9]*)\b", 1),
        // «53 - 0076» / «53 - 00414» — номер с пробелами вокруг дефиса (53-01-01, 2024)
