        (@"(?:объекте|ОРТПЦ""?)\s+(РТС\s+[А-ЯЁ][а-яё\-]{2,20})\b", 1),
        // «"Объект связи Н=39,5м"» — безымянно-номерная станция с именем-описанием (57-01-04)
        (@"[""«](Объект\s+связи[^""»]{0,30})[""»]", 1),
        // «базовая станция сотовой радиотелефонной связи РЭС 99.7 мощностью 100 Вт» —
        // радиовещательный передатчик, идентифицируемый частотой (12-РЦ-01)
        (@"связи\s+(РЭС\s+\d{2,3}[.,]\d)\s+мощностью", 1),
        // «Базовая станция БУС ФБУ "Администрация "Волго-Балт"» — БУС (бассейновый узел связи)
        // как единственный идентификатор станции (35-ВЦ-02)
        (@"[Бб]азовая\s+станция\s+(БУС)\s+ФБУ", 1),
        // «Базовая станция БС-02 R2.CC01» — заводская станция с кодом позиции (55-01-04, Омск)
        (@"([БB][CСс]-\d{2,3}\s+[A-Z]\d\.[A-Z]{2}\d{2})", 1),
