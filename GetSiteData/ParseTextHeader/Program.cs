        // «БС-N» ЦЕЛИКОМ (не только цифру) — иначе значение «2» отсеивается фильтром коротких
        // чисто цифровых номеров (1-2 цифры) ниже по конвейеру (52-НЦ-09).
        (@"([БB][CСс]-\d{1,3})\s+системы", 1),
