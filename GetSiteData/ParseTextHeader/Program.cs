        (@"№\s*(\d{2}_\d{3,}[A-Za-zА-ЯЁ0-9]*)", 1),
        // «№ PL_18_00629GUL18L21L26» — буквы_цифры_цифры+буквенный суффикс (18-УЦ-02)
        (@"№\s*([A-ZА-ЯЁ]{1,4}_\d{2,}_\d{2,}[A-Za-zА-ЯЁ0-9]*)", 1),
        // «(БС) - OBL28097_Belonogovo» / «БС- R001276» — латинский код после «БС -» (28-22-03, 63-СЦ-04)
        (@"[БB][CСс]\)?\s*-\s*([A-Z]{1,4}\d{3,}(?:_[A-Za-z0-9]+)*)\b", 1),
        // «№ OBL7488_Kostino» — код_ИмяЛатиницей (38-ИЦ-06)
        (@"№\s*([A-Z]{2,4}\d{3,}_[A-Za-z][A-Za-z0-9_]*)", 1),
        // «№ 20 (размещение)» / «№97 (модернизация)» — короткий номер перед типом работ (33-ВЛ-02)
        (@"№\s*(\d{1,4})\s*\((?:размещение|модернизация|строительство|реконструкция)\)", 1),
        // «Базовая станция 40- 00787DL18 сети» — пробел после дефиса внутри номера (40-01-05);
        // внутренний пробел убирается нормализацией в TryExtractBsNumber
        (@"[Бб]азов\w*\s+станци\w*\s+(\d{2,3}-\s?\d{3,}[A-Za-zА-ЯЁ0-9]*)", 1),
        // «№ № ELZ3348AUL_Zvezdn10» — задвоенный «№» через пробел (41-КЦ-08)
        (@"№\s*№\s*([A-Z]{2,4}\d{3,}[A-Za-z0-9_]*)", 1),
        // «№ ТМ 0015Т "Вах"» — буквы + цифры с буквенным хвостом перед именем в кавычках (72-ОЦ-01)
        (@"№\s*([А-ЯЁA-Z]{1,4}\s+\d{3,5}[А-ЯЁA-Z]?)\s*[""«]", 1),
        // «связи ХМ1032/ХМ0989L "Петролстарт"» — пара кодов через слэш без «№» (86-ХЦ-23)
        (@"связи\s+([А-ЯЁA-Z]{2}\d{3,}[A-Za-z]*/[А-ЯЁA-Z]{2}\d{3,}[A-Za-z]*)\s*[""«]", 1),
        // «№-26-00941L18L21L26L26TDD» / «№ 02- 1038DUL18L21» — дефис сразу после № или пробел
        // после дефиса внутри номера (26-01-05, 02-БЦ-01)
        (@"№\s*-?\s*(\d{2,3}-\s?\d{3,}[A-Za-zА-ЯЁ0-9]*)", 1),
        // «Базовая станция AS0367ПАО "Ростелеком"» / «SR001176ПАО» / «DR3451по адресу» — код СЛИТНО
        // с последующим словом, без пробела (30-АЦ-02, 63-СЦ-04, 80-01-05)
        (@"[Бб]азовая\s+станция\s+([A-Z]{2}\d{3,})(?=ПАО|ООО|АО|по\s)", 1),
        // «БS№MS5859» — «БS» с латинской S (OCR), затем №+код (77-01-09)
        (@"Б[SС]\s*№\s*([A-ZА-ЯЁ]{1,4}\d{3,})\b", 1),
        // «БС КУ площадка 156 км» — идентификация по площадке (16-11-10, Нижнекамскнефтехим)
        (@"[БB][CСс]\s+([А-ЯЁ]{2}\s+площадка\s+\d{1,4}\s*км)", 1),
        // «Базовая станция №29 ПАО "МегаФон"» / «№41 (выносные сектора) ПАО» — короткий номер
        // прямо перед оператором (03-БЦ-10, Бурятия)
        (@"№\s*(\d{1,4})\s*(?:\(выносные\s+сектора\)\s*)?(?:ПАО|ООО|АО|ЗАО)\s*""", 1),
        // «БС N 51180 "ПмК_Чус Калино"» — латинская N вместо № (59-55-20)
        (@"[БB][CСс]\s+N\s*(\d{4,})\b", 1),
        // «базовой станции УС 972 км МН "Куйбышев-Тихорецк"» / «УС "Зимовники"» — узлы связи
        // Связьтранснефти: номер по километражу нефтепровода или имя узла (61-РЦ-06)
        (@"станци\w*\s+(УС\s+\d{2,4}\s*км)\b", 1),
        (@"станци\w*\s+(УС\s+""[^""]{2,30}"")", 1),
        // «базовой станции ПРТО ПРС-66 "Федоровка"» — промежуточная радиостанция (68-01-03)
        (@"ПРТО\s+(ПРС-\d+)\b", 1),
        // «Базовая станция №1, абонентские станции АС №1-АС №8» (89-01-03, СИБУРТЮМЕНЬГАЗ)
        (@"№\s*(\d{1,2})\s*,\s*абонентск", 1),
        // «БС сотовой связи 15839 "НвО-Кочетова-20"» — задвоенное «БС сотовой связи» (53-01-01)
        (@"[БB][CСс]\s+сотовой\s+связи\s+(\d{4,6})\s*[""«]", 1),
        // «Базовая станция FIO61, по адресу» — код перед запятой (82-01-01, Крымаэронавигация)
        (@"[Бб]азовая\s+станция\s+([A-Z]{2,4}\d{2,4})\s*,", 1),
        // «(радиоэлектронное средство) НМ0295 ООО "Т2 Мобайл"» — код сразу после пометки РЭС (86-ХЦ-23)
        (@"\(радиоэлектронное\s+средство\)\s*[-–]?\s*([A-ZА-ЯЁ]{2}\d{3,}(?:_[A-Za-z0-9]+)?)\b", 1),
