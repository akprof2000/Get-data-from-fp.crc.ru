    // Предкомпилированные версии паттернов — создаются один раз, переиспользуются для всех файлов.
    private static readonly (Regex Rx, int Group)[] BsNumberPatterns =
        [.. BsNumberPatternSources.Select(p => (new Regex(p.Pattern, RegexOptions.IgnoreCase | RegexOptions.Compiled), p.Group))];

    // Постобработка захваченного значения номера (не зависят от содержимого документа — можно
    // предкомпилировать раз и навсегда, в отличие от контекстных проверок ниже, где паттерн
    // строится из значения текущего кандидата и потому не может быть статическим).
    [GeneratedRegex(@"(\d)\s+-\s*(\d)")]
    private static partial Regex InnerSpaceDashRx();
    [GeneratedRegex(@"-\s+(\d)")]
    private static partial Regex DashSpaceDigitRx();
    [GeneratedRegex(@"^\d+\.\d{2,5}$")]
    private static partial Regex DotNumericFormatRx();
    // Две ЛЮБЫЕ буквы подряд (не только заглавные): «86 ст. Москва-Пассажирская-Курская»
    // содержит точку, но не содержит двух заглавных подряд — с прежним [A-ZА-ЯЁ]{2} такое
    // значение отбрасывалось фильтром точек как «число-техпараметр» (77-ОМ-04).
    [GeneratedRegex(@"[A-Za-zА-ЯЁа-яё]{2}")]
    private static partial Regex TwoLettersRx();
    [GeneratedRegex(@"^\d{1,2}$")]
    private static partial Regex OneOrTwoDigitsRx();
    [GeneratedRegex(@"^\d{4}$")]
    private static partial Regex FourDigitsRx();

    private static string? TryExtractBsNumber(string text)
    {
        foreach (var (rx, group) in BsNumberPatterns)
        {
            var m = rx.Match(text);
            if (!m.Success) continue;
            var value = (group == 0 ? m.Value : m.Groups[group].Value).Trim();
            // Убираем внутренние пробелы в номере (напр. «64- 01361» → «64-01361»)
            value = InnerSpaceDashRx().Replace(value, "$1-$2");
            value = DashSpaceDigitRx().Replace(value, "-$1");
            // «5 и СКЗ (104,6 км)» → «5 (104,6 км)»: «и СКЗ» (станция катодной защиты) — пояснение
            // в тексте, а не часть номера (14-01-01, Якутия/Интер РАО).
            value = value.Replace(" и СКЗ ", " ");
            if (value.Length > 60) continue;
            // Точка в значении допустима для форматов «ЦЦ.ЦЦЦ»—«ЦЦ.ЦЦЦЦЦ» (70.195, 22.4437) и для
            // буквенно-цифровых кодов («БС-02 R2.CC01», «РЭС 99.7»): фильтр отсекает только чисто
            // числовые значения с точкой не по формату — это техпараметры, а не номера станций.
            if (value.Contains('.')
                && !DotNumericFormatRx().IsMatch(value)
                && !TwoLettersRx().IsMatch(value)) continue;
            // Отсеиваем слишком короткие чисто цифровые значения (1-2 цифры — это не номер БС)
            // Исключение: если в тексте после числа идёт «сети»/«стандарт»/«, абонентские» —
            // это реальный номер (БС № 17; «Базовая станция №1, абонентские станции» 89-01-03).
            // Между номером и этими словами допускаются уточнение в скобках и название оператора:
            // «Базовая станция №41 (выносные сектора) ПАО "МегаФон" сети подвижной…» — раньше
            // требовалось «сети» вплотную к номеру, и такой номер терялся (03-БЦ-10, Бурятия).
            // Эти три проверки зависят от РАСПОЗНАННОГО значения (Regex.Escape(value) меняется
            // на каждый вызов), поэтому паттерн строится динамически и предкомпилировать его нельзя.
            bool hasNetworkContext = Regex.IsMatch(text,
                $@"№\s*{Regex.Escape(value)}\s*(?:\([^)]{{0,40}}\)\s*)?(?:(?:ПАО|ООО|ОАО|АО|ЗАО)\s*""[^""]{{1,40}}""\s*)?(?:\s*(?:сети|стандарт)|,\s*абонентск)", RegexOptions.IgnoreCase);
            if (!hasNetworkContext && OneOrTwoDigitsRx().IsMatch(value)) continue;
            // Отсеиваем строки, похожие на технические параметры, а не на номера
            // Исключение: если в исходном тексте после числа идёт имя в кавычках — это точно номер БС
            bool isNamedStation = Regex.IsMatch(text,
                $@"№\s*{Regex.Escape(value)}\s+""[A-ZА-ЯЁa-zа-яё]", RegexOptions.IgnoreCase);
            // «№ 2064» — знак «№» перед числом однозначно указывает на номер БС, а не на год
            // (год после «№» в русском тексте не пишут). Без этого исключения «БС ... № 2064,
            // размещаемой по адресу» ошибочно отбрасывалось как «год» (52-НЦ-09 и десятки других
            // документов Т2 Мобайл, где номер БС случайно попадает в диапазон 2000-2099).
            bool hasNumberSign = Regex.IsMatch(text,
                $@"№\s*{Regex.Escape(value)}\b", RegexOptions.IgnoreCase);
            if (!isNamedStation && !hasNumberSign && FourDigitsRx().IsMatch(value) && int.TryParse(value, out var yr) && yr is >= 2000 and <= 2099) continue; // год
            return value.TrimEnd('/').Trim();
        }
        return null;
    }

    // ── Helpers ────────────────────────────────────────────────────────

    private static string GetSubDir(FileInfo fileInfo)
    {
        var relativePath = GetRelativePath(fileInfo.FullName);
        return Path.GetDirectoryName(relativePath) ?? string.Empty;
    }

    private static bool IsAlreadyProcessed(FileInfo fileInfo)
    {
        var subDir = GetSubDir(fileInfo);
        lock (_logLock)
        {
            if (_processedByDir.TryGetValue(subDir, out var set) && set.Contains(fileInfo.Name))
                return true;
        }
        // Проверяем наличие готового JSON-файла на диске
        var relativePath = GetRelativePath(fileInfo.FullName);
        var jsonRel = Path.ChangeExtension(relativePath, ".json");
        if (File.Exists(Path.Combine(OutputJsonPath, jsonRel))
            || File.Exists(Path.Combine(OutputErrorsPath, jsonRel)))
        {
            lock (_logLock)
            {
                if (!_processedByDir.TryGetValue(subDir, out var set))
                    _processedByDir[subDir] = set = [];
                set.Add(fileInfo.Name);
            }
            return true;
        }
        return false;
    }

    private static string GetRelativePath(string fullPath)
    {
        var basePath = Path.GetFullPath(InputBasePath);
        var full = Path.GetFullPath(fullPath);
        if (full.StartsWith(basePath, StringComparison.OrdinalIgnoreCase))
            return full[basePath.Length..].TrimStart(Path.DirectorySeparatorChar, Path.AltDirectorySeparatorChar);
        return fullPath;
    }

    private static void EnsureDir(string filePath)
    {
        var dir = Path.GetDirectoryName(filePath);
        if (!string.IsNullOrEmpty(dir)) Directory.CreateDirectory(dir);
    }

    private static void WriteJson(string path, object data)
    {
        var tmp = path + ".tmp";
        File.WriteAllText(tmp, JsonSerializer.Serialize(data, JsonOpts));
        File.Move(tmp, path, overwrite: true);
    }

    // ── Processed logs (per subdirectory) ──────────────────────────────

    /// <summary>
    /// Загружает лог-файлы из всех существующих подкаталогов OutputJson.
    /// Лог хранится в OutputJson/subDir/_processed.json
    /// </summary>
    private static void LoadAllProcessedLogs()
    {
        if (!Directory.Exists(OutputJsonPath)) return;
        foreach (var logFile in Directory.EnumerateFiles(OutputJsonPath, ProcessedLogFileName, SearchOption.AllDirectories))
        {
            // subDir относительно OutputJsonPath
            var dir = Path.GetDirectoryName(logFile) ?? OutputJsonPath;
            var subDir = Path.GetRelativePath(OutputJsonPath, dir);
            if (subDir == ".") subDir = string.Empty;
            try
            {
                var data = JsonSerializer.Deserialize<ProcessedLog>(File.ReadAllText(logFile));
                _processedByDir[subDir] = data?.Files != null
                    ? new HashSet<string>(data.Files, StringComparer.OrdinalIgnoreCase)
                    : [];
            }
            catch { _processedByDir[subDir] = []; }
        }
    }

    /// <summary>Сохраняет лог для конкретного подкаталога. Вызывается под _logLock.</summary>
    private static void SaveDirLogUnsafe(string subDir, HashSet<string> files)
    {
        var logDir = string.IsNullOrEmpty(subDir)
            ? OutputJsonPath
            : Path.Combine(OutputJsonPath, subDir);
        Directory.CreateDirectory(logDir);
        var logPath = Path.Combine(logDir, ProcessedLogFileName);
        var log = new ProcessedLog { LastUpdate = DateTime.Now, Files = [.. files] };
        WriteJson(logPath, log);
    }

    /// <summary>
    /// Отмечает файл обработанным только в памяти (без диск-I/O). Реальное сохранение
    /// откладывается до FlushDirtyLogs — так лог подкаталога переписывается на диск
    /// изредка, а не после каждого файла.
    /// </summary>
    private static void MarkProcessed(string subDir, string fileName)
    {
        lock (_logLock)
        {
            if (!_processedByDir.TryGetValue(subDir, out var dirSet))
                _processedByDir[subDir] = dirSet = [];
            dirSet.Add(fileName);
            _dirtyDirs.Add(subDir);
        }

        if (Interlocked.Increment(ref _filesSinceFlush) % FlushEveryNFiles == 0)
            FlushDirtyLogs();
    }

    /// <summary>Записывает на диск логи всех подкаталогов, изменившихся с прошлого сохранения.</summary>
    private static void FlushDirtyLogs()
    {
        List<(string SubDir, HashSet<string> Files)> toFlush;
        lock (_logLock)
        {
            if (_dirtyDirs.Count == 0) return;
            // Снимок содержимого — дальнейшую запись на диск делаем без удержания блокировки,
            // чтобы не тормозить остальные потоки, продолжающие обрабатывать файлы.
            toFlush = [.. _dirtyDirs.Select(d => (d, new HashSet<string>(_processedByDir[d], StringComparer.OrdinalIgnoreCase)))];
            _dirtyDirs.Clear();
        }

        foreach (var (subDir, files) in toFlush)
            SaveDirLogUnsafe(subDir, files);
    }

    private class ProcessedLog
    {
        public DateTime LastUpdate { get; set; }
        public List<string> Files { get; set; } = [];
    }
