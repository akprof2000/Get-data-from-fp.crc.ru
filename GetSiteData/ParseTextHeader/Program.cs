        (@"связи\s+(\d{2,3}-\d{2,3})\s+Ретранслятор", 1),
        // «...ООО "Газпром трансгаз Томск" УРС-2 "Новокузнецкое ЛПУМГ"» — узел радиосвязи Газпрома (42-21-02)
