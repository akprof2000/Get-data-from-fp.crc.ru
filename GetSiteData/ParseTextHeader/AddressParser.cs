using System.Text.RegularExpressions;

namespace ParseTextHeader;

/// <summary>
/// Извлекает и очищает адрес размещения базовой станции из текста документа.
/// Основной список паттернов (<see cref="AddressPatterns"/>) перебирается по очереди —
/// от самых специфичных к самым широким; первый валидный результат побеждает.
/// Вспомогательные проверки (<see cref="IsValidAddress"/>, <see cref="Clean"/>,
/// <see cref="TrimToStopWord"/>) переведены на предкомпилированные <c>GeneratedRegex</c>:
/// это горячий путь, вызываемый на каждый кандидат для каждого из ~112 тыс. документов,
/// а инлайновых литеральных паттернов здесь было полсотни — далеко за пределами
/// встроенного кэша Regex (15 записей), что означало постоянную перекомпиляцию.
/// </summary>
public static partial class AddressParser
{
    // Стоп-слова, после которых адрес заканчивается (признак начала технического текста).
    // Хранятся сразу в нижнем регистре — раньше ToLowerInvariant() вызывался для каждого
    // слова на КАЖДЫЙ вызов TrimToStopWord (то есть на каждый документ), это лишние ~60
    // аллокаций строк там, где сравнение и так регистронезависимое по построению.
    private static readonly string[] StopWordsLower =
    [
        // Технические пояснения по установке
        "(базовая станция", "(антенны", "(оборудование", "(рес ",
        "в термобоксе", "на трубостойк", "на парапет", "на надстройк",
        "размещается в", "устанавливается", "расположена в",
        "антенны на", "оборудование в",
        // Уточнения места/опоры/расстояния после адреса.
        // «столб/башня/АМС/мачта» БОЛЬШЕ НЕ стоп-слова: это часть адреса-описания
        // («…ул. Кирова, АМС восточнее д. 16», «…ул. Минская, столб ПАО "ВымпелКом"»,
        // «…дома 1; башня АО "ПБК"»), их обрезка теряла место установки (54-НС-07,
        // 59-55-20, 60-01-06). Технический хвост-владелец после них (координаты,
        // стандарты, юрлицо после точки) срезают отдельные правила Clean.
        "антенная опора", "опора двойного",
        ", одн ",           // ОДН (опора двойного назначения)
        // Голое «, опора »/«опора в » НЕ в стоп-словах: слово «опора» бывает и частью описания
        // места станции («опора между д. 13 по ул. Ясеневая…», «опора в районе д. 50»), и
        // техническим хвостом-владельцем («опора АО "НБК"», «опора МО МР…»). Различаются они
        // умной обрезкой OpoRaJunkRx в TrimToStopWord, а не простым стоп-словом (77-01-09).
        ", участок ",       // «, участок б/н»
        ", ртс ",           // «, РТС Пахомово»
        ", сопк ",          // «, СОПК "Ремонтник"»
        " ус \"",           // «УС "Самара РНУ"» — узел связи
        // Технические данные, идущие сразу за адресом.
        // «координат» — корень (покрывает «координаты/координатам/по координатам/
        // с координатами»): после снятия стоп-слов опор эти хвосты потекли в адрес
        // за «мачта … , с координатами: 57.26…» (69-01-01 и др.).
        "кадастровый номер", "координат", "географические координаты",
        // «характеристик» и «расположение техническ»/«расположены на» — техописание
        // после опоры: «АМС ПАО "ВымпелКом", с техническими характеристиками…»,
        // «АМС … . Расположение технических средств: TDQ-…» (79-01-02, 03-БЦ-10).
        "характеристик", "расположение техническ", "расположены на",
        "стандарт", "стандартов", "стандарта",
        " аму ",
        "согласно",
        "по приложению",        // «…, АМС ИО Эверест, по приложению» (41-КЦ-08)
        "находятся антенн", "антенны сторонн", "на которой размещ",  // мусор после опоры (77-01-09)
        "в части организаци",   // «..., в части организации санитарно-защитной зоны»
        "в пределах",           // «..., в пределах в районе водной станции» (15-01-09)
        // «вблизи» режем только с уточнением-объектом: голое «, вблизи ул.Щегловская засека, 55»
        // — легитимная часть адреса (71-ТЦ-04), а «, Вблизи здания хранилища зерна» — тех. хвост
        "вблизи здания", "вблизи строения", "вблизи хранилища",
        "по титулу",            // «...пос. Андреевка по титулу проекта: "Создание ВСМ..."» (77-ОМ-04)
        "с учетом установки",   // «...ул. Комсомольская, с учетом установки FM передатчика» (66-01-32)
        "с учётом установки",
        // Технические хвосты, ошибочно прицепленные к адресу (02-БЦ-01 и др.)
        "год ввода", "год реконструкц", "год модернизац",
        "сведения об объекте", "сведения о реконструкц",
        "(реконструкц", "(модернизац",
        " координаты",          // «..., д. 17. Координаты WGS-84: …» (без двоеточия тоже)
        "wgs-84", "wgs 84", "(wgs",          // «..., д. 17. WGS-84: 50.59…» / «( WGS-84: …)»
        "технические характеристик",         // «…д. 4. Технические характеристики передающего…»
        "публичное акционерное", "акционерное общество",   // хвост-владелец после адреса
        "общество с ограниченной",
        "разработчик:",         // «...ул. Ленина 113а". Разработчик: ООО ЭкспаГрупп"...» — хвост-
                                // разработчик с его юр.адресом; без обрезки IsValidAddress отвергал
                                // весь кандидат из-за слова «юридическ» в этом хвосте (85-99-85)
        "владелец прто", "владелец объекта", "владелец рэс",   // «...дома 5 Владелец ПРТО: ПАО «МТС»...» (27-ФУ-02)
        "см.приложение", "см. приложение", "см.прил",          // «...16 см.Приложение» (обратный порядок, 2024 год)
        ", месторождение",  // «с. Снежное, месторождение Кайен» (87-01-03)
        // Уточнения расстояния в скобках после адреса: «(координаты: …)», «(55.871385, …)»
        "(координаты", "(географические",
        // ВАЖНО: простых стоп-слов «м/км + направление» здесь НЕТ намеренно. Раньше они были и
        // резали адрес РАНЬШЕ умной обрезки DistanceClauseRx, которая проверяет хвост на населённый
        // пункт: «в 3100 м восточнее д. Безобразовка» рубилось до «в 3100», теряя деревню (68-01-03).
        // Всю обрезку дистанций делает DistanceClauseRx + LocationDescRx в TrimToStopWord.
        // Технические хвосты после адреса (из разбора «хороших» документов с грязными адресами)
        " н=",                       // «…жилое здание Н=26 м» (68-01-03, 16-11-10)
        " высотой",                  // «…АМС ПАО "ВымпелКом" высотой 72 м» (38-ИЦ-06)
        "на высоте",                 // «…д. 25, на высоте 19-23 м» (77-01-09)
        "; антенн", ", антенны и",   // «…14); антенны и блоки RRU будут размещены…» (38-ИЦ-06)
        "состав и характеристики",   // «…и Кирова". Состав и характеристики те…» (59-55-20)
        "установлены антенн", "размещены антенн",   // «…установлены антенны ООО "Т2…» (77-01-09)
        " на кровле",                // «…д.34к8 На кровле здания по адресу…» (77-01-09)
    ];

    // Regex для числовых уточнений расстояния («55м восточнее», «14,6км северо-западнее», «0,055 км севернее»)
    // Ищем только когда перед «м» или «км» стоит цифра (не буква д/м с точкой — это дом/метро).
    // Lookbehind (?<=\d) гарантирует, что «м» принадлежит числу, а не сокращению.
    [GeneratedRegex(@",?\s*(?:вблизи\s+(?:в\s+)?)?\d+[,.]?\d*\s*(?:метр\w+|(?<=\d)\s*км\.?|(?<=\d)\s*м\.?)(?:\s+(?:северо?-?\w*|южн\w*|восточн\w*|западн\w*|на\s+\S+))?", RegexOptions.IgnoreCase)]
    private static partial Regex DistanceClauseRx();

    // «трасс»/«шоссе»/«переезд»/«пост» — километраж дороги — часть адреса, а не хвост дистанции.
    [GeneratedRegex(@"\b(?:д\.|дом\w*|с\.|пгт\.?|посёлок|поселок|деревня|село|аул|хутор|ст\.|станица|п\.|здани\w*|строени\w*|трасс\w*|шоссе|автодорог\w*|переезд\w*|пост\b|ул\.|улиц\w*|ориентир\w*|пер\.|проспект|пр-т|наб\w*)\s?", RegexOptions.IgnoreCase)]
    private static partial Regex LocationDescRx();

    // «опора …» — обычно технический хвост после адреса (владелец/тип/высота опоры), который надо
    // отрезать («…ул. Гагарина, опора в районе д. 50», «…пос. Вожский, опора МО МР…»). НО в
    // московских документах модернизации сам адрес станции задан ЧЕРЕЗ опору: «г. Москва, опора
    // между д. 13 по ул. Ясеневая…» — здесь резать нельзя, иначе остаётся «г. Москва» (<10 симв →
    // отбраковка → extract падал на юр.адрес разработчика в Перми/Балашихе, 77-01-09). Различаем по
    // ДЛИНЕ адреса ДО «опора»: короткий префикс (<15 симв, т.е. только регион-город) ⇒ «опора»
    // несёт адрес, не режем; длинный ⇒ адрес самодостаточен, «опора …» — хвост, режем.
    // Требуем запятую/пробел ПЕРЕД «опора» (не дефис и не «(»), иначе резалось составное слово:
    // «столб-опора ВК» → «столб-», «сооружение (опора) ООО» → «сооружение (». Отдельное слово-хвост
    // «…, опора АО …» / «…ул. Гагарина опора» по-прежнему ловится (64-01-02, 13-01-04).
    // Словоформы «опора/опоре/опорой/опору/опоры» — «…на ЖБ опоре ПАО "ВымпелКом", Н=26 м»
    // тоже технический хвост (16-11-10); именительный «опора» покрывает московские адреса-через-опору.
    [GeneratedRegex(@"(?:,\s*|\s+)опор(?:а|е|ы|у|ой)\b", RegexOptions.IgnoreCase)]
    private static partial Regex OpoRaRx();

    // Маркер улицы/проезда — если он ЕСТЬ в префиксе до «опора», значит опора уже хвост (адрес есть).
    [GeneratedRegex(@"\b(?:ул\.|улиц\w*|пр-т|пр-кт|проспект|шоссе|переул\w*|пер\.|бул\w*|б-р|наб\w*|проезд|тракт|аллея|мкр)\b", RegexOptions.IgnoreCase)]
    private static partial Regex StreetMarkerRx();

    // Паттерны «по адресу: …» — ищем сначала в приложении (более точный адрес).
    // Порядок важен: более специфичные — раньше.
    private static readonly (string Pattern, RegexOptions Opts)[] AddressPatterns =
    [
        // «2. Наименование РЭС и место расположения: БС № ХМ620/ХМ0499L "Островная" ООО "..." стандартов GSM..., ХМАО-Югра, г...»
        // — адрес идёт через точку или запятую после перечисления стандартов (86-ХЦ-23).
        // Захват до КОНЦА СТРОКИ ([^\n]+), а не до первой точки: с прежним [^\n.]+ точка в
        // сокращении «г.» обрывала адрес — «Ленинградская область, г» (47-13-04); технические
        // хвосты за адресом срезают стоп-слова и Clean().
        // Разделитель НЕ должен быть точкой сокращения «г.»: иначе ленивый префикс дотягивается
        // до «…ХМАО-Югра, г.» и захват начинается с «Ханты-Мансийск» — регион теряется (86-ХЦ-23).
        // (?<!\bг)\. запрещает такую точку; если паттерн из-за этого не срабатывает вовсе —
        // документ корректно обрабатывают следующие паттерны (скобочный, «по адресу:» и т.д.).
        (@"[Нн]аименование\s+(?:РЭС|ПРТО)[^\n]*?(?:(?<!\bг)\.|,)\s*((?:ХМАО|ЯНАО|Ханты|Ямало|[А-ЯЁ][а-яё]+(?:ская|цкая|ная|ский|ций)\s+(?:область|край|округ|Республика))[^\n]+)", RegexOptions.IgnoreCase),
        // «Базовая станция сотовой связи № 45464,\nНовосибирская область, ...» — адрес на след. строке (54-НС-07)
        (@"[Бб]азовая\s+станция\s+\w+\s+связи\s+№\s*\d+\s*,\s*\n\s*([А-ЯЁ][^\n;]+(?:область|край|Республика|округ|район)[^\n;]*)", RegexOptions.IgnoreCase | RegexOptions.Multiline),
        // «БС № 24.11232 "Молодежь" по адресу: ...» — в строке ПРТО (24.49.33)
        (@"ПРТО\s*-\s*БС\s*№\s*[\d.]+\s*""[^""]*""\s+по\s+адресу:\s*([^\n(]+(?:,\s*[^\n(]+){1,5})", RegexOptions.IgnoreCase),
        // «Адрес объекта:г. Москва, Бауманская улица, дом 32, с. 2 (торговый объект)» —
        // явная метка адреса станции; двоеточие бывает без пробела, регион — «г. Москва»,
        // который под общий «адрес…:»-паттерн с областями не подходит (77-01-09).
        // Хвост «. Координаты (WGS84): …»/«Разработчик:» срезают стоп-слова и Clean().
        (@"[Аа]дрес\s+объекта\s*:\s*([^\n;]{10,200})", RegexOptions.IgnoreCase),
        // «Адрес: г. Москва, Бауманская улица, дом 32, с. 2» — та же метка в начале строки
        // приложения. Якорь на начало строки, чтобы не совпадать с «Юридический адрес:».
        (@"(?:^|\n)\s*Адрес\s*:\s*([^\n;]{10,200})", RegexOptions.Multiline),
        // «Местоположение: Новосибирская область, …» (54-НС-07)
        (@"[Мм]естоположение\s*:\s*([^\n(]+(?:область|край|Республика|округ|ХМАО|ЯНАО|обл\.)[^\n(]+)", RegexOptions.IgnoreCase),
        // «Место расположения РЭС: РФ, Херсонская область, Генический МО, …» (84-99-01, МирТелеком)
        (@"[Мм]есто\s+расположения\s+РЭС\s*:\s*(?:РФ,\s*)?([^\n*]+(?:область|край|Республика|округ)[^\n*]+)", RegexOptions.IgnoreCase),
        // «место ее расположения (адрес): … Волгоградская область, город Камышин, ул. …»
        (@"место\s+(?:ее\s+)?расположения\s*\(адрес\)\s*:[^,\n]*,\s*([А-ЯЁа-яё][^,\n]*(?:область|край|Республика|округ|ХМАО|ЯНАО|обл\.)[^,\n]*(?:,\s*[^,()\n]+){1,6})", RegexOptions.IgnoreCase),
        // «место расположения ПРТО/РЭС» — явная метка в приложении
        // Ленивый {0,3}? — захватываем ПЕРВУЮ адресную строку после метки, а не перепрыгиваем
        // её до строки «3. Год ввода … 2020г.» (которая ложно проходит из-за «г.») (02-БЦ-01)
        // «Место расположения ПРТО (адрес): 39.5 км северо-западнее ...» — «(адрес)» опционально
        // между меткой ПРТО/РЭС и двоеточием (14-01-01, Якутия)
        (@"(?:место\s+расположения\s+(?:ПРТО|РЭС)\s*(?:\(адрес\))?|наименование\s+(?:ПРТО|РЭС)\s+и\s+место\s+расположения\s*(?:\(адрес\))?)[:\s]*(?:[^\n]+\n){0,3}?([^\n]+(?:область|край|Республика|округ|ХМАО|ЯНАО|г\.|ул\.|пр-т|пр\.|бул\.|пер\.|пл\.|ш\.)[^\n]*)", RegexOptions.IgnoreCase | RegexOptions.Singleline),
        // «БС … "имя": Регион, город, улица» (адрес после двоеточия)
        (@"[БB][CСс]\s*№?\s*[\w\.\-""«»/]+\s*""[^""]*""\s*:\s*([^\n,]+(?:,\s*[^\n,]+){1,6})", RegexOptions.IgnoreCase),
        // «расположенной: Ставропольский край, ...» — причастие+двоеточие без «по адресу» (26-01-05, 2024 год)
        (@"расположенн\w*\s*:\s*([^\n(]+(?:,\s*[^\n(]+){0,6})", RegexOptions.IgnoreCase),
        // «...стандарта ... ПАО "МТС". Республика Башкортостан, Бураевский район, с. Бураево, ул. Колхозная, д. 17,
        // столб. Координаты...» — адрес сразу после кавычек оператора (с точкой или без), без «по адресу».
        // Высокий приоритет: должен срабатывать раньше generic «адрес:» (который иначе ловит «Адрес: 109147,
        // … г. Москва, ул. Марксистская» — юр.адрес владельца, см. 02-БЦ-01-000-Т-000099).
        // Требуем, чтобы прилагательное было ИМЕННО перед словом-регионом (область/край/округ/АО),
        // иначе «Нижегородский филиал» (где «филиал» — не регион) ложно матчился как адрес.
        // Точка перед завершающим словом опциональна: некоторые поля обрываются прямо перед
        // «СООТВЕТСТВУЕТ»/переводом строки без точки (17-01-04: «...ПАО "МТС"\n\nСООТВЕТСТВУЕТ»).
        (@"(?:ПАО|ООО|АО|ЗАО)\s+""[^""]{2,40}""\.?\s*((?:Республика\s+[А-ЯЁ][а-яёА-ЯЁ\-\s]*?|[А-ЯЁ][а-яё]+(?:ская|цкая|ской|кий)\s+(?:область|край|округ|обл\.|АО))[^\n(]*?)(?=\s*\(|\.?\s*(?:Коорд|Геогр|Систем|Радиоподсистем|СООТВЕТСТВУЕТ)|\n)", RegexOptions.IgnoreCase),
        // «Республика Саха (Якутия), 39.5 км северо-западнее с. Нюя Северная, ул. Первомайская, д. 14»
        // — название республики СО СКОБКОЙ «(Якутия)», из-за которой обычные паттерны с [^\n(]
        // обрывались. Захват до «площадка»/«согласно»/конца строки (14-01-01, Интер РАО)
