        // которая может встретиться и ВНУТРИ «Приложения», а не только в отдельной секции
        // «Фирма-разработчик» (см. 19-01-01-000-Т-000034: захватывало адрес разработчика вместо
        // настоящего «Республика Хакасия, г.Абакан, ул.Пушкина, д.57»).
        (@"(?<!адрес\s{0,3}:\s{0,3})(?<!заказчик:\s{0,3})([А-ЯЁ][а-яёА-ЯЁ\-]+(?:\s+[А-ЯЁ][а-яё]+)?\s+(?:Республика|Респ\.?|область|край|обл\.?)\s*,\s*[^\n(]+(?:,\s*[^\n(]+){0,5})", RegexOptions.IgnoreCase),
        // «...LTE-1800/LTE-2600 г. Севастополь, ул. Степаняна, дом 7/1» / «...LTE-2600 Севастополь, ул. ...» —
        // Севастополь как город федерального значения, без «Республика»/«область» перед ним и часто
        // без знака препинания перед адресом (82-01-01, много в 2024)
        (@"((?:г\.\s*)?Севастополь\s*,\s*[^\n(]+(?:,\s*[^\n(]+){0,4})", RegexOptions.IgnoreCase),
        // «Республика Крым. г. Феодосия. пгт. Щебетовка. гора Арды-Кая. Башня» — сегменты адреса разделены
        // ТОЧКАМИ, а не запятыми (OCR-артефакт, массово в крымских документах К-телеком, 2024 год).
        // Не входит в широкие фоллбэки: якорь «Республика Крым» достаточно специфичен (в этой серии
        // юр.адрес разработчика всегда «Самарская область», совпадений не будет).
        (@"(Республика\s+Крым[.,]\s*[^\n(]+?)(?=\s*\(|\.\s*(?:Технич|СООТВЕТСТВУЕТ)|\n)", RegexOptions.IgnoreCase),
        // «...принадлежащая ПАО "ВымпелКом", г. Пенза, ул. Каракозова, д. 18, столб ИО» — городской
        // адрес сразу после оператора в кавычках, без слова-региона (область/край) и без «по адресу»
        // (58-02-02, Пенза, много документов 2024–2025). В широких фоллбэках (restricted, не для
        // шага 3): в полнотекстовом поиске матчился бы «Разработчик: ПАО "ВымпелКом", г. Москва,
        // ул. Восьмого Марта» из секции «Фирма-разработчик».
        (@"(?:ПАО|ООО|АО|ЗАО)\s+""[^""]{2,40}""\s*[,.]\s*(г\.?\s+[А-ЯЁ][а-яё\-]+\s*,\s*(?:ул|пр|пер|ш|наб|б-р)[^\n(]{3,100})", RegexOptions.IgnoreCase),
        // «Владимирская, г. Владимир, ул. Студеная Гора, д. 36А» / «Алтайский, Шипуновский р-н,
        // c. Шипуново…» — регион-ПРИЛАГАТЕЛЬНОЕ без слова «область/край» (обрезано в исходнике).
        // Широкий фоллбэк (restricted): в полнотекстовом поиске мог бы цеплять любые прилагательные
        // на «-ская» из техтекста. Латинская «c» в «c. Шипуново» — OCR-омограф (33-ВЛ-02, 22-01-14).
        (@"\b([А-ЯЁ][а-яё]+(?:ская|ский)\s*,\s*(?:[А-ЯЁ][а-яё]+(?:ский|ской)\s+(?:р-н|район)|г\.|[cс]\.|пгт|д\.)\s*[^\n()]{2,80})", RegexOptions.None),
    ];

    // Предкомпилированные адресные паттерны — один раз на весь прогон.
    private static readonly Regex[] AddressRegexes =
        [.. AddressPatterns.Select(p => new Regex(p.Pattern, p.Opts | RegexOptions.Compiled))];

    // Последние 6 паттернов («Республика …» без метки «по адресу»/оператора, обратный порядок слов
    // «Алтай Республика, …» и т.п.) — широкие фоллбэки без текстового якоря. В полнотекстовом поиске
    // (шаг 3 в Extract) это опасно: они могут найти адрес владельца/разработчика в секции
    // «Фирма-разработчик» (которая не входит в срезы шагов 1–2, но входит в fullText целиком).
    // Поэтому в шаге 3 их не используем — см. 23-КК-10-000-Т-000049, где так находило
    // «Республика Дагестан, г. Махачкала, …» (адрес ИП-разработчика) вместо настоящего адреса станции.
    private const int BroadFallbackPatternCount = 6;
    private static readonly Regex[] AddressRegexesRestricted =
        AddressRegexes[..^BroadFallbackPatternCount];

    /// <summary>
    /// Пытается извлечь сырой адрес из текста документа.
    /// </summary>
    public static string? Extract(string fullText, IReadOnlyList<string> firstLines)
    {
        // 1. Ищем в разделе «Приложение» — там адрес обычно чище
        // Регистрозависимый поиск: заголовок секции всегда «Приложение» с заглавной буквы, а
        // случайные упоминания внутри предложения — строчные («...(приложение)», «см. приложение»,
        // «согласно приложению»). С IgnoreCase здесь ловилось такое случайное упоминание ВНУТРИ
        // «Проектной документации», из-за чего срез шага 1 расширялся и включал «Фирма-разработчик»
        // (19-01-01-000-Т-000034: «...комната 33(приложение)» перед фактическим «Фирма-разработчик»).
        var appendixStart = fullText.IndexOf("Приложение", StringComparison.Ordinal);
        if (appendixStart >= 0)
        {
            var result = TryPatterns(fullText[appendixStart..]);
            if (result != null) return result;
        }

        // 2. Ищем в разделе «Проектная документация» (66-01-32, 87-01-03: адрес только там)
        var projDocStart = fullText.IndexOf("Проектная документация", StringComparison.OrdinalIgnoreCase);
        if (projDocStart >= 0)
        {
            var projEnd = fullText.IndexOf("СООТВЕТСТВУЕТ", projDocStart, StringComparison.OrdinalIgnoreCase);
            var projSlice = projEnd > projDocStart
                ? fullText[projDocStart..projEnd]
                : fullText[projDocStart..Math.Min(projDocStart + 1500, fullText.Length)];
            var result = TryPatterns(projSlice);
            if (result != null) return result;
        }

        // 3. Фоллбэк: ищем во всём тексте (покрывает документы без Приложения).
        // Без широких фоллбэков (см. AddressRegexesRestricted) — здесь в область поиска
        // попадает и «Фирма-разработчик», их использование там небезопасно.
        {
            var result = TryPatterns(fullText, restricted: true);
            if (result != null) return result;
        }

        // 4. Строка из первых 12 строк с явными географическими признаками
        var geoLine = firstLines.FirstOrDefault(l =>
            (l.Contains("Республика") || l.Contains("область") || l.Contains("обл.") ||
             l.Contains("край") || l.Contains("округ")) &&
            (l.Contains("г.") || l.Contains("ул.") || l.Contains("пр-т") || l.Contains("д.")));

        if (geoLine != null)
        {
            var candidate = TrimToStopWord(geoLine);
            // Без валидации этот фоллбэк брал заголовок документа («Материалы по модернизации …»),
            // обрезанный по стоп-слову «стандарт». Проверяем, что это похоже на адрес. (33-ВЦ-02)
            if (IsValidAddress(candidate))
                return Clean(candidate);
        }

        return null;
    }

    private static string? TryPatterns(string text, bool restricted = false)
    {
        foreach (var rx in restricted ? AddressRegexesRestricted : AddressRegexes)
        {
            var m = rx.Match(text);
            if (m.Success)
            {
                var candidate = TrimToStopWord(m.Groups[1].Value);
                if (IsValidAddress(candidate))
                    return Clean(candidate);
            }
        }
        return null;
    }

    /// <summary>Обрезает строку адреса при появлении технических стоп-слов.</summary>
    private static string TrimToStopWord(string raw)
    {
        var lower = raw.ToLowerInvariant();
        int cutAt = raw.Length;
        foreach (var sw in StopWordsLower)
        {
            int idx = lower.IndexOf(sw, StringComparison.Ordinal);
            if (idx > 0 && idx < cutAt)
                cutAt = idx;
        }
        // «опора …» — хвост режем, НО не когда адрес задан ЧЕРЕЗ опору («г. Москва, опора между
        // д. 13 по ул. Ясеневая…»). Признак «адрес через опору»: короткий префикс (< 15 симв, т.е.
        // только регион-город) И в нём ещё НЕТ улицы. Если улица до опоры уже есть («проспект Мира,
        // опора в районе д. 61»), опора — хвост, режем (77-01-09: иначе терялось «г. Москва» и
        // цеплялся мусор «…находятся антенны сторонних операторов…» из приложения).
        // «опора …» БОЛЬШЕ не режем как хвост — она часть адреса-описания места, как
        // столб/башня/АМС/мачта («…ул. Советская, опора АО "Русские Башни"», «…д. 62б,
        // на антенной опоре ПАО "МТС"», московские «г. Москва, опора между д. 13…»,
        // 50-99-02, 23-КК-10, 77-01-09). Технический хвост ПОСЛЕ опоры (координаты,
        // высота «Н=», стандарты, «находятся антенны сторонних…», разработчик) режут
        // отдельные стоп-слова и правила Clean.
        // Числовые уточнения расстояния: «, 55м восточнее», «, 14,6км северо-западнее адреса:»
        // Пропускаем если за совпадением (в пределах 50 символов) есть населённый пункт:
        // «1,8 км юго-восточнее д. Чистостем» — не обрезаем, т.к. д. Чистостем — часть адреса
        foreach (Match distMatch in DistanceClauseRx().Matches(raw))
        {
            if (distMatch.Index > 5 && distMatch.Index < cutAt)
            {
                int endIdx = distMatch.Index + distMatch.Length;
                var tail = endIdx < raw.Length ? raw[endIdx..].TrimStart() : string.Empty;
                // Смотрим до 50 символов за совпадением — есть ли населённый пункт
                var tailSlice = tail.Length > 50 ? tail[..50] : tail;
                // «дома» (без сокращения «д.») — частая форма в описаниях вида «50 м севернее дома 98»
                // (82-01-01: без неё резался хвост «(АМС 50 м севернее дома 98)» до просто «(АМС»)
                bool isLocationDesc = LocationDescRx().IsMatch(tailSlice);
                if (!isLocationDesc)
                {
                    cutAt = distMatch.Index;
                    break;
                }
            }
        }
        return raw[..cutAt];
    }

