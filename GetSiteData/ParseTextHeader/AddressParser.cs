    [GeneratedRegex(@"\s*в\s+соответстви", RegexOptions.IgnoreCase)]
    private static partial Regex TailVSootvetstviiRx();
    [GeneratedRegex(@"\s*,?\s*\(\s*\d{2,3}[.,]\d{3,}°?\s*[,;]\s*\d{2,3}[.,]\d{3,}°?\s*\)\s*$")]
    private static partial Regex TrailingBareCoordsRx();
    [GeneratedRegex(@"\s*\(\s*(?:WGS[- ]?84:?\s*)?\d{1,3}[.,]\d{3,}.*$", RegexOptions.Singleline)]
    private static partial Regex MidStringCoordsRx();
    // ── Хвосты-координаты и огрызки в конце адреса (координаты уже лежат в поле coordinates) ──
    // «…с. Латоново (47°29'29.4"с.ш.; 38°40'6.2"в.д.)» — DMS в скобках, закрывающая может отсутствовать
    [GeneratedRegex(@"\s*,?\s*\([^()]*\d{1,3}\s*°[^()]*\)?\s*$")]
    private static partial Regex TrailingDmsParenRx();
    // «…д. 285, 53°15'00.42" С.Ш., 50°15'18.86" В.Д» — DMS без скобок в конце (63-СЦ-04)
    [GeneratedRegex(@"[,\s]+\d{1,3}\s*°\s*\d{1,2}[^\n]*$")]
    private static partial Regex TrailingDmsBareRx();
    // «…мачта РТРС, 49.013731 с.ш. 131.070867 в.д» — десятичные координаты БЕЗ градуса
    // и без скобок, с текстовым направлением с.ш./в.д. в конце (79-01-02).
    [GeneratedRegex(@"[,\s]+\d{2,3}[.,]\d{3,}\s*[°]?\s*с\.?\s*ш\.?[^\n]*?в\.?\s*д\.?\.?\s*$", RegexOptions.IgnoreCase)]
    private static partial Regex TrailingDecDirRx();
    // «…столб 28м ЗАО "Русские Башни", с.ш. 59.857725 в.д. 30.218913» — направление
    // ПЕРЕД числом (с.ш. X в.д. Y), координаты в конце адреса (78-01-05).
    [GeneratedRegex(@"[,\s]+с\.?\s*ш\.?\s*\d{2,3}[.,]\d{3,}\s*в\.?\s*д\.?\s*\d{2,3}[.,]\d{3,}\s*$", RegexOptions.IgnoreCase)]
    private static partial Regex TrailingDirDecRx();
    // Хвост «(коорд. …)»/«коорд. …» — со слова «координаты/коорд.», ЗА которым идут ЦИФРЫ
    // или скобка/двоеточие с цифрами (координаты уже в поле coordinates). Требование цифр
    // после слова не даёт задеть улицу «Координатная» (05-01-01, 16-11-10).
    [GeneratedRegex(@"[,;\s]*\(?\s*(?:гео[-\s]?)?коорд(?:инаты|\.)?\s*[:№]?\s*[-\d(].*$", RegexOptions.IgnoreCase | RegexOptions.Singleline)]
    private static partial Regex TrailingKoordRx();
    // Координатный хвост с нестандартным градусом (°, «град.», «*»): начинается СРАЗУ с числа
    // координаты (разделитель + \d), поэтому НЕ захватывает адрес назад. Требуем направление
    // (с.ш./в.д./N/E) в пределах хвоста. «…(55град.30'55.2"С.Ш., 49град.…В.Д.)»,
    // «…, 56.4255° N, 37.7847° Е», «…; 51.751013°N, 36.221015°Е».
    [GeneratedRegex(@"[,;(]\s*\(?\s*\d{1,3}[.,]?\d*\s*(?:°|град\.?|\*)[^,;()]{0,30}?(?:[СЮ]\.?\s*Ш|[NS])\b[^()]*?\)?\s*$", RegexOptions.IgnoreCase)]
    private static partial Regex TrailingAnyDegRx();
    // Десятичная пара с латинским направлением N…E (с градусом ° или без): начинается с
    // разделителя+числа, НЕ захватывает адрес назад. «…СНТ "Каштан" 55.546218° N, 38.917368° E»,
    // «…67.826944N, 58.417778E», «…, 55.476925 N, 37.717784 Е» (кир. «Е» тоже).
    [GeneratedRegex(@"[,;\s]+\d{2,3}[.,]\d{3,}\s*°?\s*N\s*[,;]?\s*\d{2,3}[.,]\d{3,}\s*°?\s*[EЕ]\.?\s*$", RegexOptions.IgnoreCase)]
    private static partial Regex TrailingLatNERx();
    // «…в/ч 23626, 55.542711, 36.926180» — голая пара десятичных координат в конце (77-МО-02)
    [GeneratedRegex(@",\s*\d{2,3}\.\d{4,}\s*,\s*\d{2,3}\.\d{4,}\s*$")]
    private static partial Regex TrailingDecPairRx();
    // «…в районе д. 4, 24:02:7101004» — кадастровый номер в конце (24-49-33)
    [GeneratedRegex(@",?\s*\d{1,2}:\d{1,2}:\d{4,}[\d:]*\s*$")]
    private static partial Regex TrailingCadastralRx();
    // «…участок 26 (кадастровый № отсутствует)» — скобка-примечание о кадастре: убираем
    // ТОЛЬКО скобку, номер участка «26» остаётся частью адреса (24-49-33).
    [GeneratedRegex(@"\s*\((?:кадастров|земельн)[^()]*\)?\s*$", RegexOptions.IgnoreCase)]
    private static partial Regex TrailingCadastralParenRx();
    // «…д. 32г, зу находится по кад. № 26:12:…» — обрывок «зу/земельный участок … по кад…»:
    // техническое примечание, а не адрес (26-01-05).
    [GeneratedRegex(@",?\s*(?:зу|з/у|земельн\w*\s+участок)\b[^,]*?(?:кад\w*\.?\s*№?|кадастров\w*)[^,]*$", RegexOptions.IgnoreCase)]
    private static partial Regex TrailingCadastralWordsRx();
    // «…корп 2 (» / «…от д. 32 (» / «…б/н (примерно» / «…(60 град» — огрызок незакрытой скобки
    [GeneratedRegex(@"\s*\([^()]{0,20}$")]
    private static partial Regex TrailingParenStubRx();
    // Висячая аббревиатура владельца в конце без имени: захват адреса обрывается на
    // кавычке имени, оставляя «…ул. Костычева, АМС ПАО», «…дом. 2, столб, АО» (54-НС-07,
    // 15-01-09). Убираем сам обрывок владельца, а затем — оставшуюся без уточнения опору.
    [GeneratedRegex(@"[,\s]+(?:ПАО|ОАО|АО|ООО|ЗАО|ИП|ФГУП|МУП|ГУП|НАО)\.?\s*$")]
    private static partial Regex TrailingOwnerStubRx();
    // Опора без уточнения в самом конце («…ул. Костычева, АМС», «…, столб») — после
    // удаления висячего владельца она не несёт адресной информации.
    [GeneratedRegex(@"[,\s]+(?:АМС|столб|башня|мачта|опора|вышка)\.?\s*$", RegexOptions.IgnoreCase)]
    private static partial Regex TrailingBareOporaRx();
    // Номер БС в начале адреса: «Базовая станция № SA0149 Сахалинская область…»,
    // «BTS-70-00477 Томская область…» — имя станции, а не часть адреса (65-С1-08, 70-ТС-07).
    [GeneratedRegex(@"^\s*(?:[Бб]азовая\s+станция\s*)?№?\s*[A-ZА-ЯЁ]{2,4}[-\s]?\d[\w\-/]*\s*,?\s+(?=[А-ЯЁ])")]
    private static partial Regex LeadingStationNumberRx();
    // Обрывок «Гео»/«Географ…» в конце — начало срезанного «Географические координаты»
    // («…д. 1, АМС ПАО "МТС" Гео» → «…д. 1, АМС ПАО "МТС"»). Требуем пробел/запятую перед,
    // чтобы не задеть «Геологическую»/«Геодезическую» улицу (там слово не в конце после разделителя).
    [GeneratedRegex(@"[\s,]+Гео(?:граф\w*)?\.?\s*$")]
    private static partial Regex TrailingGeoStubRx();
    // Висячий предлог/уточнение без объекта в конце адреса: «…, вблизи», «…, антенна», «…, около».
    // \b ОБЯЗАТЕЛЕН: без него «на» съедало окончание слова — «ул. Гагарина» → «ул. Гагари»,
    // «ул. Молодежная» → «ул. Молодеж». Требуем ещё запятую/пробел перед словом-предлогом.
    // «по|с|с техническими» добавлены: остаются висячими после обрезки тех.хвоста
    // стоп-словом («…мачта ПАО "МегаФон", с координатами:…» → срез «координат…» →
    // «…, с» → убираем «, с»; «…, по координатам:» → «…, по»; 69-01-01, 50-99-02, 79-01-02).
    // Добавлены «в» и сокращения без названия («…, пер», «…, ул», «…, д») — остаются
    // обрывками, когда захват оборвался на середине элемента адреса (26-01-05, 52-НЦ-09).
    [GeneratedRegex(@"(?:,|\s)\s*\b(?:вблизи|около|напротив|антенна|на\s+антенной|в\s+районе|между|на\s+жб|на|по|в|с(?:\s+техническими)?|пер|ул|д|дом|стр|корп|лит|зд)\.?\s*$", RegexOptions.IgnoreCase)]
    private static partial Regex DanglingPrepositionRx();

    // Однозначные сокращения республик в начале адреса → полная форма. В приложениях
    // операторы иногда пишут «РД, г. Махачкала…» вместо «Республика Дагестан…» (05-01-01).
    // Только НЕдвусмысленные: РД=Дагестан, КБР, КЧР, РСО=Сев. Осетия, РМЭ=Марий Эл,
    // РИ=Ингушетия. РБ/РК/ЧР/РТ намеренно НЕ трогаем — у них по 2+ толкования.
    [GeneratedRegex(@"^\s*РД\s*,", RegexOptions.IgnoreCase)]
    private static partial Regex RdPrefixRx();
    [GeneratedRegex(@"^\s*КБР\s*,", RegexOptions.IgnoreCase)]
    private static partial Regex KbrPrefixRx();
    [GeneratedRegex(@"^\s*КЧР\s*,", RegexOptions.IgnoreCase)]
    private static partial Regex KchrPrefixRx();

    private static string ExpandRegionAbbr(string address)
    {
        if (RdPrefixRx().IsMatch(address)) return RdPrefixRx().Replace(address, "Республика Дагестан,");
        if (KbrPrefixRx().IsMatch(address)) return KbrPrefixRx().Replace(address, "Кабардино-Балкарская Республика,");
        if (KchrPrefixRx().IsMatch(address)) return KchrPrefixRx().Replace(address, "Карачаево-Черкесская Республика,");
        return address;
    }

    public static string Clean(string address)
    {
        if (string.IsNullOrWhiteSpace(address)) return address;
        address = address.Replace('\n', ' ').Replace('\r', ' ').Replace('\t', ' ');
        // Убираем множественные пробелы
        address = WhitespaceRunRx().Replace(address, " ");

        // Отсекаем мусорный префикс до метки «по адресу:» — ТОЛЬКО когда префикс явно паразитный
        // (имя станции/оператора/филиала, попавшее в захват вместе с адресом: «базовая станция
        // № 66039 … ПАО "ВымпелКом" по адресу: Чеченская…», «Филиал ПАО "МТС" в г. Санкт-Петербург
        // по адресу: Ленинградская область…») или служебный («расположенная по адресу: …»).
        // Junk-маркер (станци/№/ПАО"/Филиал) главнее наличия региона в префиксе: у филиала регион
        // («г. Санкт-Петербург») — это регион ФИЛИАЛА, а не станции (78-01-47). Легитимные же
        // «Хабаровский край, … в 20 м от дома по адресу: Белорусская улица, 33» junk-маркеров
        // не содержат и не трогаются (27-99-24, 52-СЦ-04).
        var byAddr = ByAddressPrefixRx().Match(address);
        if (byAddr.Success)
        {
            var prefix = byAddr.Groups["prefix"].Value;
            bool junkPrefix = prefix.Length == 0
                || JunkPrefixRx().IsMatch(prefix)
                || (prefix.Contains('№') && !PrefixRegionRx().IsMatch(prefix));
            var rest = address[byAddr.Length..];
            if (junkPrefix && rest.Trim().Length >= 10)
                address = rest;
        }
        // Убираем повторяющиеся запятые
        address = RepeatedCommaRx().Replace(address, ",");
        // Добавляем пробел после запятой, если его нет: «область,Задонский» → «область, Задонский».
        // (?<!\d) — не трогаем десятичные запятые вида «4,74 км» (иначе ломались реальные дистанции,
        // превращая «4,74» в «4, 74», см. 48-20-01: «Липецкая обл., г. Липецк, 4,74 км восточнее...»).
        address = CommaNoSpaceRx().Replace(address, ", ");
        // Убираем пробел перед запятой: «г . Сочи» → «г. Сочи», «ул . Ленина» → «ул. Ленина»
        address = SpaceBeforeDotRx().Replace(address, ".");
        // «Бауманская улица , дом 32» → «Бауманская улица, дом 32»
        address = SpaceBeforeCommaRx().Replace(address, ",");
        // Убираем лишние точки в конце перед запятой: «обл., г.» — ок; «обл..,» — нет
        address = RepeatedDotRx().Replace(address, ".");
        // Убираем хвост «далее по приложению» и подобные
        var tailMatch = TailDaleePoRx().Match(address);
        if (tailMatch.Success) address = address[..tailMatch.Index];
        // Убираем хвост «согласно приложению»
        tailMatch = TailSoglasnoRx().Match(address);
        if (tailMatch.Success) address = address[..tailMatch.Index];
        // Убираем хвост «в соответствии с приложением»
        tailMatch = TailVSootvetstviiRx().Match(address);
        if (tailMatch.Success) address = address[..tailMatch.Index];
        // Убираем хвостовые голые координаты в скобках: «…д.93/95 (51.523105, 45.965148)» / «(53.540542°, 49.342503°)»
        address = TrailingBareCoordsRx().Replace(address, "");
        // Убираем всё начиная с координат в скобках в середине строки:
        // «…д. 42 (50.597973 с.ш., 36.598212 в.д.). Публичное акционерное общество…» (Белгородская обл.)
        address = MidStringCoordsRx().Replace(address, "");
        address = address.Trim('"', '\'', ' ', '\t', ',', '.');
        // Хвосты-координаты в конце адреса (координаты уже извлечены в отдельное поле):
        // DMS в скобках → DMS без скобок → голая десятичная пара → кадастровый номер.
        address = TrailingDmsParenRx().Replace(address, "");
        address = TrailingDmsBareRx().Replace(address, "");
        address = TrailingDecDirRx().Replace(address, "");
        address = TrailingDirDecRx().Replace(address, "");
        address = TrailingKoordRx().Replace(address, "");
        address = TrailingAnyDegRx().Replace(address, "");
        address = TrailingLatNERx().Replace(address, "");
        address = TrailingGeoStubRx().Replace(address, "");
        address = TrailingDecPairRx().Replace(address, "");
        address = TrailingCadastralRx().Replace(address, "");
        // Кадастровые примечания в конце: «(кадастровый № отсутствует)» — убираем скобку,
        // номер участка остаётся; «зу находится по кад. № …» — весь обрывок (24-49-33, 26-01-05).
        address = TrailingCadastralParenRx().Replace(address, "");
        address = TrailingCadastralWordsRx().Replace(address, "");
        // Огрызок незакрытой скобки в конце («…корп 2 (», «…(примерно», «…(60 град»)
        address = TrailingParenStubRx().Replace(address, "");
        // Висячая закрывающая скобка без открывающей: «…ул. Школьная, дом 29)» (11-РЦ-09)
        if (address.EndsWith(')') && !address.Contains('('))
            address = address[..^1];
        // Незакрытая скобка: захват оборвался внутри пояснения — «…д. 33б (инфраструктура
        // ООО "Инфрасеть" для БС», «…стадион "Луч" (существующие трубостойки, закреплённые
        // на существующем столбе». Обрезаем всё от последней непарной «(» — это пояснение,
        // а не адрес (45-01-06, 42-21-02, 16-11-10).
        if (address.Count(c => c == '(') > address.Count(c => c == ')'))
        {
            var open = address.LastIndexOf('(');
            if (open > 10)
                address = address[..open];
        }
        // Убираем висячий предлог/уточнение в конце после обрезки хвоста-опоры:
        // «…ул. Верхняя, вблизи опора ПБК» → (обрезка «опора …») → «…ул. Верхняя, вблизи» →
        // «…ул. Верхняя». Такой предлог без объекта не несёт адресной информации (26-01-05, 64-01-02).
        // Висячий владелец без имени, затем оставшаяся без уточнения опора
        // («…ул. Костычева, АМС ПАО» → «…ул. Костычева»).
        address = TrailingOwnerStubRx().Replace(address, "");
        address = TrailingBareOporaRx().Replace(address, "");
        // Имя станции в начале адреса («Базовая станция № SA0149 Сахалинская обл.…»)
        address = LeadingStationNumberRx().Replace(address, "");
        address = DanglingPrepositionRx().Replace(address, "");
        // ; в набор Trim: концевая точка с запятой — мусор («…с. Зерново;» → «…с. Зерново»),
        // но внутренняя легитимна («…дома 1; башня …»), поэтому режем только по краям.
        address = address.Trim('"', '\'', ' ', '\t', ',', '.', ';');
        // Восстанавливаем закрывающую кавычку: имя объекта в кавычках («…завод "Агрохолдинг
        // Туймада"», «…ФГУП "РТРС"») теряет её при обрезке хвоста и финальном Trim. Если
        // двойных кавычек нечётное число — значит закрывающую срезали, возвращаем.
        if (address.Count(c => c == '"') % 2 == 1)
            address += "\"";
        // Разворачиваем однозначные сокращения республик в начале («РД,» → «Республика Дагестан,»)
        address = ExpandRegionAbbr(address);
        if (address.Length > 300)
            address = address[..300] + "...";
        return address.Trim();
    }
