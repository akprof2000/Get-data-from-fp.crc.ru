    /// <summary>Адрес считается валидным если содержит хотя бы один географический признак.</summary>
    private static bool IsValidAddress(string s)
    {
        if (string.IsNullOrWhiteSpace(s) || s.Trim().Length < 10) return false;
        var trimmed = s.TrimStart();
        // Отсеиваем строки начинающиеся с технических заголовков
        if (ProjectDocHeaderRx().IsMatch(trimmed)) return false;
        if (DeveloperHeaderRx().IsMatch(trimmed)) return false;
        if (SzzHeaderRx().IsMatch(trimmed)) return false;
        // Заголовки документов, ошибочно принимаемые за адрес (33-ВЦ-02 и др.)
        if (MaterialsHeaderRx().IsMatch(trimmed)) return false;
        if (WorkingDocHeaderRx().IsMatch(trimmed)) return false;
        if (ProjectHeaderRx().IsMatch(trimmed)) return false;
        if (RpHeaderRx().IsMatch(trimmed)) return false;
        // «Филиал ПАО "МТС" в г. Санкт-Петербург» — подразделение владельца, а не адрес станции;
        // регион в нём — регион филиала. Бракуем, чтобы сработал следующий паттерн с настоящим
        // «по адресу: Ленинградская область…» (78-01-47).
        if (trimmed.StartsWith("Филиал", StringComparison.OrdinalIgnoreCase)) return false;
        // Метки административных/юридических блоков владельца или разработчика — никогда не часть
        // реального адреса станции. Если они попали в кандидат, это адрес другого лица. (13-01-04 и др.)
        // «юридическ» без требования «адрес» рядом — ловит и «Юридический и фактический адрес:» (67-СО-01).
        // «почтовый адрес» НЕ отбрасываем целиком: «Почтовый адрес ориентира: <реальный адрес>» — легитимный
        // кадастровый формат (38-ИЦ-06); опасен только в связке с «юридическ» (владелец/разработчик).
        if (s.Contains("юридическ", StringComparison.OrdinalIgnoreCase)
            || s.Contains("заказчик:", StringComparison.OrdinalIgnoreCase)
            || s.Contains("разработчик проекта", StringComparison.OrdinalIgnoreCase)
            // ИНН/ОГРН в кандидате — это юридические реквизиты владельца, а не адрес станции:
            // «109147, г. Москва, ул. Марксистская, д. 4, ИНН 7740000076, ОГРН…» (36-ВЦ-20).
            || s.Contains("ИНН", StringComparison.Ordinal)
            || s.Contains("ОГРН", StringComparison.Ordinal))
            return false;
        // Признак региона
        bool hasRegion = s.Contains("область", StringComparison.OrdinalIgnoreCase)
            || s.Contains("обл.", StringComparison.OrdinalIgnoreCase)
            || OblWordRx().IsMatch(s)   // «Псковская обл,» без точки
            || s.Contains("край", StringComparison.OrdinalIgnoreCase)
            || s.Contains("Республика", StringComparison.OrdinalIgnoreCase)
            || s.Contains("Респ", StringComparison.OrdinalIgnoreCase)
            || s.Contains("округ", StringComparison.OrdinalIgnoreCase)
            || s.Contains("ХМАО", StringComparison.OrdinalIgnoreCase)
            || s.Contains("ЯНАО", StringComparison.OrdinalIgnoreCase)
            || EaoWordRx().IsMatch(s)   // Еврейская автономная область (79-01-02)
            || s.Contains(" АО", StringComparison.OrdinalIgnoreCase)   // Чукотский АО
            || s.Contains("АО,", StringComparison.OrdinalIgnoreCase)
            || s.Contains("ЛНР", StringComparison.OrdinalIgnoreCase)   // Луганская НР (81-99-01)
            || s.Contains("ДНР", StringComparison.OrdinalIgnoreCase)   // Донецкая НР
            || s.Contains("Луганская", StringComparison.OrdinalIgnoreCase)
            || s.Contains("Донецкая", StringComparison.OrdinalIgnoreCase)
            // Города федерального значения — сами являются регионом (78-01-05)
            || s.Contains("Санкт-Петербург", StringComparison.OrdinalIgnoreCase)
            || s.Contains("Санкт-Петербурга", StringComparison.OrdinalIgnoreCase)
            || s.Contains("Севастополь", StringComparison.OrdinalIgnoreCase)
            || MoscowWordRx().IsMatch(s)
            || GorodMoscowRx().IsMatch(s)
            // «УР, Увинский район» — Удмуртская Республика сокращённо
            || UrPrefixRx().IsMatch(s);
        // Признак населённого пункта или места
        bool hasLocality = GDotCityRx().IsMatch(s)   // «г. Уфа» / «Г. Пенза», но не «2020г.»
            || GSpaceCityRx().IsMatch(s)   // «г Астрахань» — без точки (30-АЦ-02)
            || UlSpaceRx().IsMatch(s)      // «ул Сун Ят-Сена» — без точки
            || s.Contains("город", StringComparison.OrdinalIgnoreCase)
            || s.Contains("ул.", StringComparison.OrdinalIgnoreCase)
            || s.Contains("пр-т", StringComparison.OrdinalIgnoreCase)
            || s.Contains("шоссе", StringComparison.OrdinalIgnoreCase)
            || s.Contains("СНТ", StringComparison.OrdinalIgnoreCase)
            || s.Contains("тер.", StringComparison.OrdinalIgnoreCase)
            || s.Contains("месторождение", StringComparison.OrdinalIgnoreCase)
            || s.Contains("микрорайон", StringComparison.OrdinalIgnoreCase)
            || s.Contains("Аэропорт", StringComparison.OrdinalIgnoreCase)
            || s.Contains("улица", StringComparison.OrdinalIgnoreCase)
            || s.Contains("село", StringComparison.OrdinalIgnoreCase)
            || s.Contains("посёлок", StringComparison.OrdinalIgnoreCase)
            || s.Contains("поселок", StringComparison.OrdinalIgnoreCase)
            || s.Contains("километр", StringComparison.OrdinalIgnoreCase)
            || s.Contains("рп.", StringComparison.OrdinalIgnoreCase)   // «рп. Новая Игирма»
            || s.Contains("р.п.", StringComparison.OrdinalIgnoreCase)
            || s.Contains("рабочий поселок", StringComparison.OrdinalIgnoreCase)
            || s.Contains("рабочий посёлок", StringComparison.OrdinalIgnoreCase)
            || RaionAbbrRx().IsMatch(s)    // «Куньинский р-н»
            || PgtWordRx().IsMatch(s)      // «Кунья пгт» (постфикс)
            || StanitsaWordRx().IsMatch(s)
            || PgtDottedRx().IsMatch(s)
            || PgtNamedRx().IsMatch(s)
            || PDotNamedRx().IsMatch(s)
            || SDotNamedRx().IsMatch(s)
            || PosDotNamedRx().IsMatch(s)
            || DDotNamedRx().IsMatch(s)
            || StDotNamedRx().IsMatch(s)
            || AulNamedRx().IsMatch(s)
            || ReversedCityRx().IsMatch(s)
            || ReversedStreetRx().IsMatch(s)
            || ReversedHouseRx().IsMatch(s)
            // Обратный порядок («Аскат с», «Барнаул пр», «Ялта п») — массовый формат в документах
            // 2024 года («Алтай Республика, Горно-Алтайск г, Коммунистический пр, 16»).
            || ReversedSeloRx().IsMatch(s)
            || ReversedPoselokRx().IsMatch(s)
            || ReversedProspektRx().IsMatch(s)
            || DerevnyaNamedRx().IsMatch(s)
            || s.Contains("округ", StringComparison.OrdinalIgnoreCase);
        return hasLocality || (hasRegion && s.Length > 20);
    }

