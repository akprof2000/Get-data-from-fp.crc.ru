using System.Text.RegularExpressions;

namespace ParseTextHeader;

public static class OperatorExtractor
{
    // Паттерны с явной меткой «владелец» — контекст однозначно указывает на владельца станции
    // (не разработчика, не стороннюю организацию), поэтому имя принимается без проверки по
    // белому списку известных операторов (в отличие от GenericOrgPatterns ниже).
    // [^:\n]*? (ленивый, не [^:]* жадный) — жадный класс с обратным ходом мог перепрыгнуть через
    // ПЕРВУЮ корректную пару кавычек к следующей, более далёкой (напр. «Наименование владельца ПРТО
    // (РЭС) - ПАО "ВымпелКом", юридический адрес: 127083 ... 2. ... Базовая станция № 40234 "ИНК
    // Усть-Кут"» — жадный вариант захватывал ВЕСЬ текст между кавычкой после «ВымпелКом» и следующей
    // кавычкой у «ИНК Усть-Кут» как имя оператора, 38-ИЦ-06). Ленивый класс берёт БЛИЖАЙШУЮ пару.
    // Запрет на \n дополнительно держит поиск в пределах одного предложения/абзаца (33-ВЛ-02).
    // Организационно-правовая форма (ПАО|ООО|АО|...) сделана ОБЯЗАТЕЛЬНОЙ (не опциональной): без
    // этого якоря фраза «Владелец РЭС, осуществляющий эксплуатацию РЭС ... в соответствии с п.296
    // СанПиН 2.1.3684-21 "Санитарно-эпидемиологические требования к содержанию территорий ..."»
    // (без организации рядом с меткой) находила ближайшую попавшуюся кавычку — название СанПиНа —
    // и записывала его как оператора (55-01-04, десятки документов). Обязательная форма гарантирует,
    // что найдена именно организация, а не случайная кавычка где-то дальше по тексту.
    private static readonly string[] OwnerLabeledPatterns =
    [
        @"[Пп]олное\s+наименование\s+владельца[^:\n]{0,100}?[:\s]+(?:ПАО|ООО|АО|ЗАО|МУП|ФГУП|ФГКУ|ФГБУ|ФБУ|ФКУ|ГКУ|ГУП)\s+""([^""]+)""",
        @"[Вв]ладелец\s+(?:ПРТО|РЭС|базовой\s+станции)[^:\n]{0,100}?[:\s]*(?:ПАО|ООО|АО|ЗАО|ФГКУ|ФГУП|ФГБУ|ФБУ|ФКУ|ГКУ|ГУП|МУП)\s+""([^""]+)""",
        @"[Нн]аименование\s+владельца[^:\n]{0,100}?[:\s]*(?:ПАО|ООО|АО|ЗАО|ФГКУ|ФГУП|ФГБУ|ФБУ|ФКУ|ГКУ|ГУП|МУП)\s+""([^""]+)""",
        // «Владелец: ФГКУ "Росгранстрой".» — голая метка без слова ПРТО/РЭС (27-99-24)
        @"[Вв]ладелец\s*:\s*(?:ПАО|ООО|АО|ЗАО|ФГКУ|ФГУП|ФГБУ|ФБУ|ФКУ|ГКУ|ГУП|МУП)\s+""([^""]+)""",
        // «Наименование юридического лица владельца ПРТО АО "Интер РАО - Электрогенерация"» —
        // табличная форма без двоеточия, «владельца» отделено от «Наименование» словами
        // «юридического лица» (14-01-01, Якутия)
        @"[Нн]аименование\s+юридического\s+лица[,]?\s+владельца\s+ПРТО\s+(?:ПАО|ООО|АО|ЗАО|ФГКУ|ФГУП|ФГБУ|ФБУ|ФКУ|ГКУ|ГУП|МУП)\s+""([^""]+)""",
    ];

    // Паттерны без явной метки «владелец» — просто организационно-правовая форма + название
    // в кавычках. Контекст слабее (может случайно поймать постороннюю организацию), поэтому
    // такие совпадения по-прежнему проверяются по белому списку известных операторов.
    private static readonly string[] GenericOrgPatterns =
    [
        // «Публичное акционерное общество "МТС"»
        @"[Пп]убличн\w+\s+[Аа]кционерн\w+\s+[Оо]бщество\w*\s+""([^""]+)""",
        @"[Оо]бщества?\s+с\s+ограниченной\s+ответственностью\s+""([^""]+)""",
        @"[Аа]кционерного?\s+[Оо]бщества?\s+""([^""]+)""",
        // «ПАО "МТС"» / «ООО "Т2 Мобайл"» в начале строки документа
        @"(?:ПАО|ООО|АО|ЗАО)\s+""([^""]+)""",
    ];

    // Предкомпилированные версии — один раз на весь прогон.
    private static readonly Regex[] OwnerLabeledRegexes =
        [.. OwnerLabeledPatterns.Select(p => new Regex(p, RegexOptions.IgnoreCase | RegexOptions.Compiled))];
    private static readonly Regex[] GenericOrgRegexes =
        [.. GenericOrgPatterns.Select(p => new Regex(p, RegexOptions.IgnoreCase | RegexOptions.Compiled))];

    // Формы собственности: длинные варианты (ГБУЗ НО) раньше коротких, чтобы alternation не
    // обрезал их. ГБУЗ/ФГУП/ФБУ/ГУП/МУП — ведомственные базовые станции (больницы, водные пути,
    // муниципальные службы), для которых владелец — не сотовый оператор (54-НС-04, 35-ВЦ-02).
    private static readonly Regex BsOpRegex = new(
        @"[Бб]азов(?:ая|ой)\s+станци(?:я|и)[^.]{0,150}(ПАО|ООО|ЗАО|ГБУЗ\s+[А-ЯЁ]{2}|ГБУЗ|ФГУП|ФГБУ|ФБУ|ГУП|МУП|АО)\s+""([^""]{2,40})""",
        RegexOptions.IgnoreCase | RegexOptions.Singleline | RegexOptions.Compiled);

    private static readonly Regex NetOpRegex = new(
        @"[Сс]еть\s+(?:подвижной|сотовой)[^.]{0,60}?(ПАО|ООО|АО)\s+""([^""]{2,40})""\s+стандарт",
        RegexOptions.IgnoreCase | RegexOptions.Compiled);

    // Название в кавычках иногда переносится на следующую строку в исходном тексте
    // («...предприятия "Российская\nтелевизионная и радиовещательная сеть"», 10-КЦ-01) —
    // схлопываем внутренние переводы строк/пробелы в один пробел.
    private static string NormalizeName(string s) => Regex.Replace(s, @"\s+", " ").Trim();

    public static string? Extract(string fullText)
    {
        // Ищем сначала в разделе «Приложение» — там владелец описан явно
        // Регистрозависимо — см. комментарий в AddressParser.Extract про случайные строчные
        // упоминания «(приложение)» внутри текста, из-за которых срез расширялся до «Фирма-разработчик».
        var appendixStart = fullText.IndexOf("Приложение", StringComparison.Ordinal);
        var searchText = appendixStart >= 0 ? fullText[appendixStart..] : fullText;

        // 0. Приоритет: оператор из строки «Базовая станция ... ПАО/ООО "..."»
        //    — это сетевой оператор, а не владелец опоры/ПРТО (напр. ЕКАТЕРИНБУРГ-2000).
        //    Ищем во всём тексте, т.к. в заголовке это самая надёжная строка.
        //    Контекст «Базовая станция ... <форма> "Имя"» однозначно указывает на оператора связи —
        //    принимаем имя без проверки по белому списку (напр. АО «ПБК», ПАО «Сбербанк России» —
        //    небольшие/нестандартные операторы, которых нет и не может быть в статичном списке).
        var bsOpMatch = BsOpRegex.Match(fullText);
        if (bsOpMatch.Success)
        {
            var candidate = bsOpMatch.Groups[2].Value.Trim();
            // Второй шанс канонизации — по ПОЛНОМУ тексту совпадения: при вложенных кавычках
            // («ФБУ "Администрация "Волго-Балт"») группа захватывает только «Администрация »,
            // а настоящее имя оператора остаётся за пределами группы (35-ВЦ-02).
            return KnownOperators.Canonicalize(candidate)
                   ?? KnownOperators.FindInText(bsOpMatch.Value)
                   ?? $"{bsOpMatch.Groups[1].Value} \"{candidate}\"";
        }

        // 0б. «Сеть подвижной/сотовой радиотелефонной связи ПАО "МТС" стандарта ...»
        //     — когда оператор указан перед списком стандартов, без «Базовая станция» рядом (26-01-05)
        var netOpMatch = NetOpRegex.Match(fullText);
        if (netOpMatch.Success)
        {
            var candidate = netOpMatch.Groups[2].Value.Trim();
            return KnownOperators.Canonicalize(candidate)
                   ?? $"{netOpMatch.Groups[1].Value} \"{candidate}\"";
        }

        // 1. Паттерны с явной меткой «владелец» — доверяем без проверки по белому списку
        foreach (var rx in OwnerLabeledRegexes)
        {
            var m = rx.Match(searchText);
            if (m.Success)
            {
                var raw = NormalizeName(m.Groups[^1].Value);
                return KnownOperators.Canonicalize(raw) ?? raw;
            }
        }

        // 1б. Паттерны без метки «владелец» (общая форма+кавычки) — только известные операторы
        foreach (var rx in GenericOrgRegexes)
        {
            var m = rx.Match(searchText);
            if (m.Success)
            {
                var raw = NormalizeName(m.Groups[^1].Value);
                if (KnownOperators.IsKnown(raw))
                    return KnownOperators.Canonicalize(raw) ?? raw;
            }
        }

        // 2. Проверяем имена известных операторов в «Приложении»
        var known = KnownOperators.FindInText(searchText);
        if (known != null) return known;

        // 3. Фоллбэк — ищем во всём тексте
        foreach (var rx in OwnerLabeledRegexes)
        {
            var m = rx.Match(fullText);
            if (m.Success)
            {
                var raw = NormalizeName(m.Groups[^1].Value);
                return KnownOperators.Canonicalize(raw) ?? raw;
            }
        }
        foreach (var rx in GenericOrgRegexes)
        {
            var m = rx.Match(fullText);
            if (m.Success)
            {
                var raw = NormalizeName(m.Groups[^1].Value);
                if (KnownOperators.IsKnown(raw))
                    return KnownOperators.Canonicalize(raw) ?? raw;
            }
        }

        var fallback = KnownOperators.FindInText(fullText);
        if (fallback != null) return fallback;

        // 4. Последний фоллбэк: имя оператора буквально отсутствует в тексте (пропуск при заполнении
        // формы — «сети сухопутной подвижной радиотелефонной связи ООО  стандартов...», два пробела,
        // название не подставлено), но юридический адрес/ИНН владельца ЕСТЬ и однозначно указывает
        // на известного оператора. (16-11-10, 77-01-09: десятки документов с этим пропуском в 2024 году)
        if (fullText.Contains("7743895280") || fullText.Contains("Киевское шоссе"))
            return "ООО \"Т2 Мобайл\"";

        return null;
    }
}
